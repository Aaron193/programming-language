bool loadCacheMetadataFile(const std::filesystem::path& metadataPath,
                           CacheEntryMetadata& outMetadata,
                           std::string& outError) {
    outMetadata = CacheEntryMetadata{};
    std::ifstream file(metadataPath);
    if (!file) {
        outError = "Could not open cache metadata '" + metadataPath.string() + "'.";
        return false;
    }

    std::string line;
    size_t lineNumber = 0;
    while (std::getline(file, line)) {
        ++lineNumber;
        const std::string content = stripComment(line);
        if (content.empty()) {
            continue;
        }

        const size_t equals = content.find('=');
        if (equals == std::string::npos) {
            outError = "Invalid cache metadata line " + std::to_string(lineNumber) +
                       ": expected key = value.";
            return false;
        }

        const std::string key = trim(std::string_view(content).substr(0, equals));
        const std::string value =
            trim(std::string_view(content).substr(equals + 1));

        if (key == "dependency_groups") {
            if (!parseQuotedStringArray(value, outMetadata.dependencyGroups,
                                        outError)) {
                outError = "Invalid cache metadata line " +
                           std::to_string(lineNumber) + ": " + outError;
                return false;
            }
            continue;
        }

        std::string parsed;
        if (!parseQuotedString(value, parsed, outError)) {
            outError = "Invalid cache metadata line " +
                       std::to_string(lineNumber) + ": " + outError;
            return false;
        }

        if (key == "package_id") {
            outMetadata.packageId = parsed;
        } else if (key == "version") {
            outMetadata.version = parsed;
        } else if (key == "kind") {
            outMetadata.kind = parsed;
        } else if (key == "source_type") {
            outMetadata.sourceType = parsed;
        } else if (key == "source_path") {
            outMetadata.sourcePath = parsed;
        } else if (key == "registry") {
            outMetadata.registry = parsed;
        } else if (key == "artifact_path") {
            outMetadata.artifactPath = parsed;
        } else if (key == "artifact_digest") {
            outMetadata.artifactDigest = parsed;
        } else if (key == "manifest_digest") {
            outMetadata.manifestDigest = parsed;
        } else if (key == "api_digest") {
            outMetadata.apiDigest = parsed;
        } else {
            outError = "Unsupported cache metadata field '" + key + "'.";
            return false;
        }
    }

    outMetadata.dependencyGroups =
        sortedUniqueStrings(std::move(outMetadata.dependencyGroups));
    return true;
}

bool cacheMetadataMatchesEntry(const CacheEntryMetadata& metadata,
                               const PackageRegistryEntry& entry) {
    return metadata.packageId == entry.packageId &&
           metadata.version == entry.version && metadata.kind == entry.kind &&
           metadata.sourceType == entry.sourceType &&
           metadata.sourcePath == entry.sourcePath &&
           metadata.registry == entry.registry &&
           metadata.artifactPath == entry.artifactPath &&
           metadata.artifactDigest == entry.artifactDigest &&
           metadata.manifestDigest == entry.manifestDigest &&
           metadata.apiDigest == entry.apiDigest &&
           metadata.dependencyGroups ==
               sortedUniqueStrings(entry.dependencyGroups);
}

bool addAvailableNode(std::unordered_map<std::string, PackageNode>& outNodes,
                      PackageNode node, std::string& outError) {
    auto existing = outNodes.find(node.entry.packageId);
    if (existing != outNodes.end()) {
        if (existing->second.packageDir != node.packageDir) {
            outError = "Duplicate local package '" + node.entry.packageId +
                       "' found at '" + existing->second.packageDir.string() +
                       "' and '" + node.packageDir.string() + "'.";
            return false;
        }
        return true;
    }

    outNodes[node.entry.packageId] = std::move(node);
    return true;
}

bool resolveManifestPath(const std::string& projectRoot,
                         const std::string& rawPath,
                         std::filesystem::path& outPath,
                         std::string& outError) {
    outError.clear();
    if (rawPath.rfind("file://", 0) == 0) {
        outPath = std::filesystem::path(rawPath.substr(7));
        return true;
    }

    const std::filesystem::path candidate(rawPath);
    if (candidate.is_absolute()) {
        outPath = candidate;
        return true;
    }

    if (rawPath.empty()) {
        outError = "Registry index path cannot be empty.";
        return false;
    }

    outPath = std::filesystem::path(projectRoot) / candidate;
    return true;
}

bool loadRegistryIndex(const std::string& projectRoot,
                       const ProjectRegistryConfig& config,
                       LoadedRegistryIndex& outIndex,
                       std::string& outError) {
    outIndex = LoadedRegistryIndex{};
    outError.clear();

    std::filesystem::path configuredPath;
    if (!resolveManifestPath(projectRoot, config.index, configuredPath, outError)) {
        return false;
    }

    std::error_code ec;
    if (std::filesystem::is_directory(configuredPath, ec) && !ec) {
        configuredPath /= "index.toml";
    }

    configuredPath = std::filesystem::weakly_canonical(configuredPath, ec);
    if (ec || !std::filesystem::exists(configuredPath, ec) || ec) {
        outError = "Registry '" + config.alias + "' index does not exist at '" +
                   configuredPath.lexically_normal().string() + "'.";
        return false;
    }

    outIndex.indexPath = configuredPath;
    outIndex.rootDir = configuredPath.parent_path();

    std::ifstream file(configuredPath);
    if (!file) {
        outError = "Could not open registry index '" + configuredPath.string() + "'.";
        return false;
    }

    RegistryIndexRecord current;
    bool hasCurrent = false;
    std::string line;
    size_t lineNumber = 0;

    auto flushCurrent = [&]() -> bool {
        if (!hasCurrent) {
            return true;
        }
        if (current.packageId.empty() || current.version.empty() ||
            current.artifactPath.empty() || current.artifactDigest.empty()) {
            outError = "Registry '" + config.alias +
                       "' index entries must define package_id, version, artifact_path, and artifact_digest.";
            return false;
        }
        if (!isExactPublishedVersion(current.version)) {
            outError = "Registry '" + config.alias + "' package '" +
                       current.packageId + "' must use an exact x.y.z version.";
            return false;
        }
        const std::string key = registryRecordKey(current.packageId, current.version);
        if (!outIndex.recordsByKey.emplace(key, current).second) {
            outError = "Registry '" + config.alias + "' contains a duplicate entry for '" +
                       key + "'.";
            return false;
        }
        current = RegistryIndexRecord{};
        hasCurrent = false;
        return true;
    };

    while (std::getline(file, line)) {
        ++lineNumber;
        const std::string content = stripComment(line);
        if (content.empty()) {
            continue;
        }

        if (content == "[[package]]") {
            if (!flushCurrent()) {
                return false;
            }
            hasCurrent = true;
            continue;
        }

        const size_t equals = content.find('=');
        if (equals == std::string::npos) {
            outError = "Invalid registry index line " + std::to_string(lineNumber) +
                       ": expected key = value.";
            return false;
        }

        const std::string key = trim(std::string_view(content).substr(0, equals));
        const std::string value =
            trim(std::string_view(content).substr(equals + 1));

        if (!hasCurrent) {
            if (key == "schema_version") {
                continue;
            }
            outError = "Invalid registry index line " + std::to_string(lineNumber) +
                       ": entries must appear inside [[package]].";
            return false;
        }

        if (key == "dependencies") {
            std::vector<std::string> pins;
            if (!parseQuotedStringArray(value, pins, outError)) {
                outError = "Invalid registry index line " +
                           std::to_string(lineNumber) + ": " + outError;
                return false;
            }
            current.dependencies.clear();
            for (const std::string& pinText : pins) {
                RegistryDependencyPin pin;
                if (!parseRegistryDependencyPin(pinText, pin, outError)) {
                    outError = "Invalid registry index line " +
                               std::to_string(lineNumber) + ": " + outError;
                    return false;
                }
                current.dependencies.push_back(std::move(pin));
            }
            continue;
        }

        std::string parsed;
        if (!parseQuotedString(value, parsed, outError)) {
            outError = "Invalid registry index line " + std::to_string(lineNumber) +
                       ": " + outError;
            return false;
        }

        if (key == "package_id") {
            current.packageId = parsed;
        } else if (key == "version") {
            current.version = parsed;
        } else if (key == "artifact_path") {
            current.artifactPath = parsed;
        } else if (key == "artifact_digest") {
            current.artifactDigest = parsed;
        } else if (key == "schema_version") {
            continue;
        } else {
            outError = "Unsupported registry index field '" + key + "'.";
            return false;
        }
    }

    return flushCurrent();
}

bool scanPackageDirectories(const std::filesystem::path& root,
                            std::unordered_map<std::string, PackageNode>& outNodes,
                            std::string& outError) {
    std::error_code ec;
    if (!std::filesystem::exists(root, ec) || ec) {
        return true;
    }

    for (std::filesystem::recursive_directory_iterator it(root, ec), end;
         !ec && it != end; it.increment(ec)) {
        if (ec || !it->is_directory()) {
            continue;
        }

        const std::filesystem::path dir = it->path();
        if (!fileExists(dir / kProjectManifestFileName) &&
            !fileExists(dir / "package.toml")) {
            continue;
        }

        PackageNode node;
        if (!loadPackageEntryFromDir(dir, node.entry, nullptr, outError)) {
            continue;
        }
        node.packageDir = std::filesystem::path(node.entry.packageDir);
        if (!addAvailableNode(outNodes, std::move(node), outError)) {
            return false;
        }
    }

    return true;
}

bool scanWorkspaceMemberPackages(
    const std::filesystem::path& projectRoot, const ProjectManifestData& manifest,
    std::unordered_map<std::string, PackageNode>& outNodes,
    std::unordered_set<std::string>& outWorkspacePackageIds, std::string& outError) {
    for (const std::string& member : manifest.workspaceMembers) {
        const std::filesystem::path memberPath = projectRoot / member;
        if (!isDirectory(memberPath)) {
            outError = "Workspace member '" + member + "' does not exist.";
            return false;
        }

        PackageNode node;
        if (!loadPackageEntryFromDir(memberPath, node.entry, nullptr, outError)) {
            outError = "Could not load workspace member '" + member + "': " +
                       outError;
            return false;
        }

        node.packageDir = std::filesystem::path(node.entry.packageDir);
        node.entry.sourceType = "workspace";
        node.entry.sourcePath = canonicalOrLexical(memberPath);
        if (!addAvailableNode(outNodes, node, outError)) {
            return false;
        }
        outWorkspacePackageIds.insert(node.entry.packageId);
    }

    return true;
}

const ProjectRegistryConfig* findRegistryConfig(
    const ProjectManifestData& manifest, std::string_view alias) {
    for (const auto& registry : manifest.registries) {
        if (registry.alias == alias) {
            return &registry;
        }
    }
    return nullptr;
}

bool packageIdsMatchDependencyPins(const std::vector<std::string>& manifestDeps,
                                   const std::vector<RegistryDependencyPin>& pins) {
    std::vector<std::string> pinnedIds;
    pinnedIds.reserve(pins.size());
    for (const auto& pin : pins) {
        pinnedIds.push_back(pin.packageId);
    }
    return sortedUniqueStrings(manifestDeps) == sortedUniqueStrings(std::move(pinnedIds));
}

bool resolveRegistryPackageNode(
    const std::string& projectRoot, const ProjectManifestData& manifest,
    const std::string& registryAlias, const std::string& packageId,
    const std::string& version,
    std::unordered_map<std::string, LoadedRegistryIndex>& loadedRegistryIndexes,
    std::unordered_map<std::string, PackageNode>& nodeCache, PackageNode& outNode,
    std::string& outError) {
    outNode = PackageNode{};

    if (!isExactPublishedVersion(version)) {
        outError = "Published dependency '" + packageId +
                   "' must use an exact x.y.z version in this Phase 2 slice.";
        return false;
    }

    const std::string cacheKey =
        registryAlias + "|" + registryRecordKey(packageId, version);
    auto nodeIt = nodeCache.find(cacheKey);
    if (nodeIt != nodeCache.end()) {
        outNode = nodeIt->second;
        return true;
    }

    const ProjectRegistryConfig* registry =
        findRegistryConfig(manifest, registryAlias);
    if (registry == nullptr) {
        outError = "Dependency '" + packageId + "' references unknown registry '" +
                   registryAlias + "'.";
        return false;
    }

    auto indexIt = loadedRegistryIndexes.find(registryAlias);
    if (indexIt == loadedRegistryIndexes.end()) {
        LoadedRegistryIndex loaded;
        if (!loadRegistryIndex(projectRoot, *registry, loaded, outError)) {
            return false;
        }
        indexIt = loadedRegistryIndexes.emplace(registryAlias, std::move(loaded)).first;
    }

    const auto recordIt =
        indexIt->second.recordsByKey.find(registryRecordKey(packageId, version));
    if (recordIt == indexIt->second.recordsByKey.end()) {
        outError = "Registry '" + registryAlias + "' does not contain '" + packageId +
                   "@" + version + "'.";
        return false;
    }

    const RegistryIndexRecord& record = recordIt->second;
    std::filesystem::path artifactPath;
    if (!resolveManifestPath(indexIt->second.rootDir.string(), record.artifactPath,
                             artifactPath, outError)) {
        return false;
    }
    artifactPath = canonicalOrLexical(artifactPath);
    if (!isDirectory(artifactPath)) {
        outError = "Registry artifact for '" + packageId + "@" + version +
                   "' is missing at '" + artifactPath.string() + "'.";
        return false;
    }

    const std::string actualDigest = digestDirectory(artifactPath);
    if (actualDigest.empty()) {
        outError = "Could not compute digest for registry artifact '" +
                   artifactPath.string() + "'.";
        return false;
    }
    if (actualDigest != record.artifactDigest) {
        outError = "Registry artifact digest mismatch for '" + packageId + "@" +
                   version + "'.";
        return false;
    }

    PackageNode node;
    if (!loadPackageEntryFromDir(artifactPath, node.entry, nullptr, outError)) {
        return false;
    }
    if (node.entry.packageId != packageId) {
        outError = "Registry artifact '" + artifactPath.string() +
                   "' declares package '" + node.entry.packageId +
                   "' but the index expects '" + packageId + "'.";
        return false;
    }
    if (node.entry.version != version) {
        outError = "Registry artifact '" + artifactPath.string() +
                   "' declares version '" + node.entry.version +
                   "' but the index expects '" + version + "'.";
        return false;
    }
    if (node.entry.kind != "source") {
        outError = "Published native packages are not implemented until Phase 3: '" +
                   packageId + "'.";
        return false;
    }
    if (!packageIdsMatchDependencyPins(node.entry.dependencyIds, record.dependencies)) {
        outError = "Registry artifact '" + artifactPath.string() +
                   "' has dependency IDs that do not match the registry index for '" +
                   packageId + "@" + version + "'.";
        return false;
    }

    node.packageDir = artifactPath;
    node.registryDependencies = record.dependencies;
    node.entry.sourceType = "registry";
    node.entry.sourcePath = artifactPath.string();
    node.entry.registry = registryAlias;
    node.entry.artifactPath = artifactPath.string();
    node.entry.artifactDigest = record.artifactDigest;
    node.entry.dependencyIds.clear();
    for (const auto& pin : record.dependencies) {
        node.entry.dependencyIds.push_back(pin.packageId);
    }

    nodeCache.emplace(cacheKey, node);
    outNode = std::move(node);
    return true;
}

void ensureResolvedLibraryPath(PackageNode& node, const std::string& projectRoot) {
    if (node.entry.kind != "native" || !node.entry.libraryPath.empty()) {
        return;
    }

    const std::string workspaceRoot =
        inferWorkspaceRoot(node.packageDir, node.entry, projectRoot);
    node.entry.libraryPath = canonicalOrLexical(
        std::filesystem::path(workspaceRoot) / "build" / "packages" /
        node.entry.packageNamespace / node.entry.packageName /
        kPackageLibraryFileName);
}

bool resolveDependencyNode(
    const std::string& projectRoot, const ProjectDependencySpec& dependency,
    const std::unordered_map<std::string, PackageNode>& available,
    const std::unordered_set<std::string>& workspacePackageIds, PackageNode& outNode,
    std::string& outError) {
    outNode = PackageNode{};

    if (dependency.workspace) {
        if (dependency.packageId.empty()) {
            outError = "Workspace dependency '" + dependency.alias +
                       "' must declare package = \"namespace:name\".";
            return false;
        }

        auto it = available.find(dependency.packageId);
        if (it == available.end() ||
            workspacePackageIds.find(dependency.packageId) ==
                workspacePackageIds.end()) {
            outError = "Workspace dependency '" + dependency.alias +
                       "' could not find workspace member '" +
                       dependency.packageId + "'.";
            return false;
        }
        outNode = it->second;
        outNode.entry.sourceType = "workspace";
        outNode.entry.sourcePath = outNode.packageDir.string();
    } else if (!dependency.path.empty()) {
        const std::filesystem::path dependencyPath =
            std::filesystem::path(projectRoot) / dependency.path;
        if (!loadPackageEntryFromDir(dependencyPath, outNode.entry, nullptr,
                                     outError)) {
            outError = "Could not load dependency '" + dependency.alias + "': " +
                       outError;
            return false;
        }

        outNode.packageDir = std::filesystem::path(outNode.entry.packageDir);
        outNode.entry.sourceType = "path";
        outNode.entry.sourcePath = canonicalOrLexical(dependencyPath);
    } else if (!dependency.git.empty()) {
        outError = "Dependency '" + dependency.alias +
                   "' uses git sources, which are recognized but not implemented until Phase 2.";
        return false;
    } else if (!dependency.registry.empty()) {
        outError = "Dependency '" + dependency.alias +
                   "' uses named registries, which are recognized but not implemented until Phase 2.";
        return false;
    } else if (!dependency.packageId.empty()) {
        outError = "Dependency '" + dependency.alias +
                   "' requires published package resolution, which is not implemented until Phase 2.";
        return false;
    } else {
        outError = "Dependency '" + dependency.alias + "' does not declare a usable source.";
        return false;
    }

    ensureResolvedLibraryPath(outNode, projectRoot);

    if (!dependency.packageId.empty() &&
        dependency.packageId != outNode.entry.packageId) {
        outError = "Dependency '" + dependency.alias + "' declares package '" +
                   dependency.packageId + "' but resolves to '" +
                   outNode.entry.packageId + "'.";
        return false;
    }

    if (!dependency.version.empty() && dependency.version != outNode.entry.version) {
        outError = "Dependency '" + dependency.alias + "' requires version '" +
                   dependency.version + "' but local package '" +
                   outNode.entry.packageId + "' reports '" +
                   outNode.entry.version + "'.";
        return false;
    }

    return true;
}

bool validatePackageEntry(const PackageRegistryEntry& entry,
                          const std::string& projectRoot,
                          std::string& outError) {
    const std::filesystem::path packageDir(entry.packageDir);
    if (entry.kind == "native") {
        const std::string validationRoot =
            inferWorkspaceRoot(packageDir, entry, projectRoot);
        return validatePackageDirectory(packageDir.string(), validationRoot,
                                        outError);
    }

    if (entry.entryPath.empty() || !fileExists(entry.entryPath)) {
        outError = "Source package '" + entry.importName +
                   "' is missing entry module '" + entry.entryPath + "'.";
        return false;
    }

    if (!entry.apiPath.empty()) {
        PackageApiMetadata apiMetadata;
        if (!loadPackageApiMetadata(entry.apiPath, entry.packageId, entry.importName,
                                    apiMetadata, outError)) {
            return false;
        }
    }

    return true;
}

bool materializeCacheEntry(const PackageRegistryEntry& sourceEntry,
                           const std::filesystem::path& cacheDir,
                           std::string& outError) {
    if (!removePathIfExists(cacheDir, outError)) {
        return false;
    }

    std::error_code ec;
    std::filesystem::create_directories(cacheDir, ec);
    if (ec) {
        outError = "Could not create cache directory '" + cacheDir.string() + "'.";
        return false;
    }

    const std::filesystem::path sourceDir(sourceEntry.sourcePath);
    if (sourceEntry.kind == "source") {
        if (!copyDirectoryRecursive(sourceDir, cacheDir, outError)) {
            return false;
        }
        return writeCacheMetadataFile(cacheMetadataPath(cacheDir), sourceEntry,
                                      outError);
    }

    const std::filesystem::path sourceManifest = fileExists(sourceDir / kProjectManifestFileName)
                                                     ? sourceDir / kProjectManifestFileName
                                                     : sourceDir / "package.toml";
    if (!copyFileReplacing(sourceManifest, cacheDir / kProjectManifestFileName,
                           outError)) {
        return false;
    }

    if (!sourceEntry.apiPath.empty() &&
        !copyFileReplacing(sourceEntry.apiPath, cacheDir / kPackageApiFileName,
                           outError)) {
        return false;
    }

    if (sourceEntry.libraryPath.empty()) {
        outError = "Native package '" + sourceEntry.importName +
                   "' does not have a built library to install.";
        return false;
    }

    if (!copyFileReplacing(sourceEntry.libraryPath,
                           cacheDir / kPackageLibraryFileName, outError)) {
        return false;
    }

    return writeCacheMetadataFile(cacheMetadataPath(cacheDir), sourceEntry, outError);
}

bool materializeProjectInstall(const PackageRegistryEntry& sourceEntry,
                               const std::filesystem::path& projectRoot,
                               bool offline, PackageRegistryEntry& outInstalled,
                               std::string& outError) {
    outInstalled = sourceEntry;

    const std::string digestSeed = sourceEntry.version + "|" +
                                   sourceEntry.registry + "|" +
                                   sourceEntry.artifactPath + "|" +
                                   sourceEntry.artifactDigest + "|" +
                                   sourceEntry.manifestDigest + "|" +
                                   sourceEntry.apiDigest + "|" +
                                   sourceEntry.sourcePath;
    const std::string installKey = hashString(digestSeed);
    const std::filesystem::path cacheRoot =
        preferredLocalCacheRoot(projectRoot);
    const std::filesystem::path cacheDir =
        cacheRoot / sourceEntry.packageNamespace / sourceEntry.packageName /
        installKey;
    const std::filesystem::path installDir =
        installPackagesRoot(projectRoot) / sourceEntry.packageNamespace /
        sourceEntry.packageName;

    CacheEntryMetadata metadata;
    std::string metadataError;
    const bool hasReusableCache =
        isDirectory(cacheDir) && fileExists(cacheMetadataPath(cacheDir)) &&
        loadCacheMetadataFile(cacheMetadataPath(cacheDir), metadata, metadataError) &&
        cacheMetadataMatchesEntry(metadata, sourceEntry);

    if (!hasReusableCache) {
        if (offline && sourceEntry.sourceType == "registry") {
            outError = "Dependency '" + sourceEntry.packageId +
                       "' cannot be installed with --offline because its registry artifact is not cached locally.";
            return false;
        }
        if (!materializeCacheEntry(sourceEntry, cacheDir, outError)) {
            return false;
        }
    }

    if (!removePathIfExists(installDir, outError)) {
        return false;
    }
    if (!ensureParentDirectory(installDir, outError)) {
        return false;
    }

    if (!copyDirectoryRecursive(cacheDir, installDir, outError)) {
        return false;
    }

    outInstalled.packageDir = installDir.lexically_normal().string();
    outInstalled.sourcePath = canonicalOrLexical(sourceEntry.sourcePath);
    outInstalled.artifactPath = canonicalOrLexical(sourceEntry.artifactPath);

    const std::filesystem::path installedApi = installDir / kPackageApiFileName;
    if (fileExists(installedApi)) {
        outInstalled.apiPath = installedApi.lexically_normal().string();
    } else {
        outInstalled.apiPath.clear();
    }

    if (sourceEntry.kind == "source") {
        std::filesystem::path relativeEntry =
            std::filesystem::relative(std::filesystem::path(sourceEntry.entryPath),
                                      std::filesystem::path(sourceEntry.sourcePath));
        outInstalled.entryPath = (installDir / relativeEntry).lexically_normal().string();
        outInstalled.libraryPath.clear();
    } else {
        outInstalled.libraryPath =
            (installDir / kPackageLibraryFileName).lexically_normal().string();
        outInstalled.entryPath.clear();
    }

    return validatePackageEntry(outInstalled, projectRoot.string(), outError);
}

