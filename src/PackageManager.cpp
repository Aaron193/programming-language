bool collectResolvedPackages(
    const std::string& projectRoot, const ProjectManifestData& manifest,
    const InstallOptions& options, std::vector<PackageRegistryEntry>& outEntries,
    std::string& outError) {
    outEntries.clear();
    outError.clear();

    std::unordered_map<std::string, PackageNode> available;
    if (!scanPackageDirectories(std::filesystem::path(projectRoot) / "packages",
                                available, outError)) {
        return false;
    }

    std::unordered_set<std::string> workspacePackageIds;
    if (!scanWorkspaceMemberPackages(std::filesystem::path(projectRoot), manifest,
                                     available, workspacePackageIds,
                                     outError)) {
        return false;
    }

    std::unordered_map<std::string, LoadedRegistryIndex> loadedRegistryIndexes;
    std::unordered_map<std::string, PackageNode> resolvedRegistryNodes;
    std::vector<DependencyRoot> roots;
    auto appendDependencies =
        [&](const std::vector<ProjectDependencySpec>& dependencies,
            const char* groupName) -> bool {
        for (const auto& dependency : dependencies) {
            const bool isPublishedDependency =
                !dependency.packageId.empty() && dependency.path.empty() &&
                !dependency.workspace && dependency.git.empty();
            if (options.offline &&
                dependency.path.empty() && !dependency.workspace &&
                !isPublishedDependency) {
                outError = "Dependency '" + dependency.alias +
                           "' cannot be installed with --offline because it is not a local path or workspace dependency.";
                return false;
            }

            PackageNode node;
            if (isPublishedDependency) {
                const std::string registryAlias =
                    dependency.registry.empty() ? "default" : dependency.registry;
                if (!resolveRegistryPackageNode(projectRoot, manifest, registryAlias,
                                                dependency.packageId,
                                                dependency.version,
                                                loadedRegistryIndexes,
                                                resolvedRegistryNodes, node,
                                                outError)) {
                    return false;
                }
            } else {
                if (!resolveDependencyNode(projectRoot, dependency, available,
                                           workspacePackageIds, node, outError)) {
                    return false;
                }

                if (!addAvailableNode(available, node, outError)) {
                    return false;
                }
            }

            roots.push_back(DependencyRoot{std::move(node), groupName});
        }
        return true;
    };

    if (!appendDependencies(manifest.dependencies, "runtime")) {
        return false;
    }
    if (options.includeDevDependencies &&
        !appendDependencies(manifest.devDependencies, "dev")) {
        return false;
    }

    std::sort(roots.begin(), roots.end(),
              [](const DependencyRoot& lhs, const DependencyRoot& rhs) {
                  if (lhs.node.entry.packageId != rhs.node.entry.packageId) {
                      return lhs.node.entry.packageId < rhs.node.entry.packageId;
                  }
                  return lhs.group < rhs.group;
              });

    std::unordered_map<std::string, std::unordered_set<std::string>> groupsById;
    std::unordered_map<std::string, PackageRegistryEntry> chosenEntriesById;
    std::unordered_set<std::string> finished;
    std::unordered_set<std::string> inProgressPairs;
    std::vector<PackageRegistryEntry> ordered;

    std::function<bool(const PackageNode&, const std::string&)> visit =
        [&](const PackageNode& node, const std::string& group) {
            auto chosenIt = chosenEntriesById.find(node.entry.packageId);
            if (chosenIt != chosenEntriesById.end() &&
                !packageEntryMatchesLocked(node.entry, chosenIt->second)) {
                outError = "Package '" + node.entry.packageId +
                           "' resolves to conflicting versions or sources in the dependency graph.";
                return false;
            }
            chosenEntriesById.emplace(node.entry.packageId, node.entry);

            if (!groupsById[node.entry.packageId].insert(group).second) {
                return true;
            }

            const std::string pairKey = node.entry.packageId + "|" + group;
            if (!inProgressPairs.insert(pairKey).second) {
                outError = "Package dependency cycle detected at '" +
                           node.entry.packageId + "'.";
                return false;
            }

            if (node.entry.sourceType == "registry") {
                for (const auto& dependency : node.registryDependencies) {
                    PackageNode dependencyNode;
                    if (!resolveRegistryPackageNode(
                            projectRoot, manifest, node.entry.registry,
                            dependency.packageId, dependency.version,
                            loadedRegistryIndexes, resolvedRegistryNodes,
                            dependencyNode, outError)) {
                        return false;
                    }
                    if (!visit(dependencyNode, group)) {
                        return false;
                    }
                }
            } else {
                for (const std::string& dependencyId : node.entry.dependencyIds) {
                    auto dependencyIt = available.find(dependencyId);
                    if (dependencyIt == available.end()) {
                        outError = "Package '" + node.entry.packageId +
                                   "' depends on '" + dependencyId +
                                   "', but no local package provides it.";
                        return false;
                    }
                    if (!visit(dependencyIt->second, group)) {
                        return false;
                    }
                }
            }

            inProgressPairs.erase(pairKey);
            if (finished.insert(node.entry.packageId).second) {
                ordered.push_back(node.entry);
            }
            return true;
        };

    for (const auto& root : roots) {
        if (!visit(root.node, root.group)) {
            return false;
        }
    }

    for (auto& entry : ordered) {
        auto groupsIt = groupsById.find(entry.packageId);
        if (groupsIt != groupsById.end()) {
            entry.dependencyGroups = sortedUniqueStrings(
                std::vector<std::string>(groupsIt->second.begin(),
                                         groupsIt->second.end()));
        }
    }

    std::sort(ordered.begin(), ordered.end(),
              [](const PackageRegistryEntry& lhs, const PackageRegistryEntry& rhs) {
                  if (lhs.importName != rhs.importName) {
                      return lhs.importName < rhs.importName;
                  }
                  return lhs.packageId < rhs.packageId;
              });

    for (const auto& entry : ordered) {
        if (!validatePackageEntry(entry, projectRoot, outError)) {
            return false;
        }
    }

    outEntries = std::move(ordered);
    return true;
}

bool packageEntryMatchesLocked(const PackageRegistryEntry& expected,
                               const PackageRegistryEntry& actual) {
    return expected.packageId == actual.packageId &&
           expected.importName == actual.importName &&
           expected.packageNamespace == actual.packageNamespace &&
           expected.packageName == actual.packageName &&
           expected.version == actual.version &&
           expected.kind == actual.kind &&
           expected.sourceType == actual.sourceType &&
           expected.sourcePath == actual.sourcePath &&
           expected.registry == actual.registry &&
           expected.artifactPath == actual.artifactPath &&
           expected.artifactDigest == actual.artifactDigest &&
           expected.manifestDigest == actual.manifestDigest &&
           expected.apiDigest == actual.apiDigest &&
           sortedUniqueStrings(expected.dependencyIds) ==
               sortedUniqueStrings(actual.dependencyIds) &&
           sortedUniqueStrings(expected.dependencyGroups) ==
               sortedUniqueStrings(actual.dependencyGroups);
}

bool validateLockedGraph(const std::vector<PackageRegistryEntry>& resolvedEntries,
                         const std::vector<PackageRegistryEntry>& lockedEntries,
                         std::string& outError) {
    if (resolvedEntries.size() != lockedEntries.size()) {
        outError = "mog.lock is out of date with mog.toml: resolved " +
                   std::to_string(resolvedEntries.size()) + " package(s), but lockfile has " +
                   std::to_string(lockedEntries.size()) + ".";
        return false;
    }

    std::unordered_map<std::string, PackageRegistryEntry> lockedById;
    for (const auto& entry : lockedEntries) {
        lockedById[entry.packageId] = entry;
    }

    for (const auto& resolved : resolvedEntries) {
        auto lockedIt = lockedById.find(resolved.packageId);
        if (lockedIt == lockedById.end()) {
            outError = "mog.lock is missing package '" + resolved.packageId + "'.";
            return false;
        }

        if (!packageEntryMatchesLocked(resolved, lockedIt->second)) {
            outError = "mog.lock is out of date for package '" +
                       resolved.packageId + "'. Run 'mog install' to refresh it.";
            return false;
        }
    }

    return true;
}

bool materializeInstalledPackages(const std::string& projectRoot,
                                  const std::vector<PackageRegistryEntry>& resolved,
                                  const InstallOptions& options,
                                  std::vector<PackageRegistryEntry>& outEntries,
                                  std::string& outError) {
    outEntries.clear();
    outEntries.reserve(resolved.size());
    for (const auto& entry : resolved) {
        PackageRegistryEntry installedEntry;
        if (!materializeProjectInstall(entry, std::filesystem::path(projectRoot),
                                       options.offline, installedEntry,
                                       outError)) {
            return false;
        }
        outEntries.push_back(std::move(installedEntry));
    }
    return true;
}

void sortDependencies(std::vector<ProjectDependencySpec>& dependencies) {
    std::sort(dependencies.begin(), dependencies.end(),
              [](const ProjectDependencySpec& lhs,
                 const ProjectDependencySpec& rhs) {
                  return lhs.alias < rhs.alias;
              });
}

void sortRegistries(std::vector<ProjectRegistryConfig>& registries) {
    std::sort(registries.begin(), registries.end(),
              [](const ProjectRegistryConfig& lhs,
                 const ProjectRegistryConfig& rhs) {
                  return lhs.alias < rhs.alias;
              });
}

void writeDependencyTable(std::ofstream& out, const char* tableName,
                          const std::vector<ProjectDependencySpec>& dependencies) {
    if (dependencies.empty()) {
        return;
    }

    out << "\n[" << tableName << "]\n";
    for (const auto& dependency : dependencies) {
        out << dependency.alias << " = { ";
        bool needsComma = false;
        if (!dependency.path.empty()) {
            out << "path = " << quoteTomlString(normalizeRelativePath(dependency.path));
            needsComma = true;
        }
        if (dependency.workspace) {
            if (needsComma) {
                out << ", ";
            }
            out << "workspace = true";
            needsComma = true;
        }
        if (!dependency.packageId.empty()) {
            if (needsComma) {
                out << ", ";
            }
            out << "package = " << quoteTomlString(dependency.packageId);
            needsComma = true;
        }
        if (!dependency.version.empty()) {
            if (needsComma) {
                out << ", ";
            }
            out << "version = " << quoteTomlString(dependency.version);
            needsComma = true;
        }
        if (!dependency.git.empty()) {
            if (needsComma) {
                out << ", ";
            }
            out << "git = " << quoteTomlString(dependency.git);
            needsComma = true;
        }
        if (!dependency.registry.empty()) {
            if (needsComma) {
                out << ", ";
            }
            out << "registry = " << quoteTomlString(dependency.registry);
        }
        out << " }\n";
    }
}

void writeRegistryTables(std::ofstream& out,
                         const std::vector<ProjectRegistryConfig>& registries) {
    if (registries.empty()) {
        return;
    }

    for (const auto& registry : registries) {
        out << "\n[registries." << registry.alias << "]\n";
        out << "index = " << quoteTomlString(registry.index) << "\n";
    }
}

