bool loadProjectManifestData(const std::string& projectRoot,
                             ProjectManifestData& outManifest,
                             std::string& outError) {
    outManifest = ProjectManifestData{};
    outManifest.name = defaultProjectName(projectRoot);
    outManifest.description = outManifest.name + " project";
    outError.clear();

    const std::filesystem::path manifestPath =
        std::filesystem::path(projectRoot) / kProjectManifestFileName;
    std::ifstream file(manifestPath);
    if (!file) {
        outError = "Could not open project manifest '" + manifestPath.string() + "'.";
        return false;
    }

    enum class Section {
        ROOT,
        WORKSPACE,
        REGISTRY,
        DEPENDENCIES,
        DEV_DEPENDENCIES,
    };

    Section section = Section::ROOT;
    std::string currentRegistryAlias;
    std::string line;
    size_t lineNumber = 0;
    while (std::getline(file, line)) {
        ++lineNumber;
        const std::string content = stripComment(line);
        if (content.empty()) {
            continue;
        }

        if (content == "[dependencies]") {
            section = Section::DEPENDENCIES;
            continue;
        }
        if (content == "[dev-dependencies]") {
            section = Section::DEV_DEPENDENCIES;
            continue;
        }
        if (content == "[workspace]") {
            section = Section::WORKSPACE;
            continue;
        }
        if (content.rfind("[registries.", 0) == 0 && content.back() == ']') {
            const size_t prefixLength = std::string("[registries.").size();
            currentRegistryAlias =
                content.substr(prefixLength, content.size() - prefixLength - 1);
            if (currentRegistryAlias.empty()) {
                outError = "Registry sections must be named like [registries.default].";
                return false;
            }
            section = Section::REGISTRY;
            continue;
        }

        if (content.front() == '[' && content.back() == ']') {
            outError = "Unsupported project manifest section '" + content + "'.";
            return false;
        }

        const size_t equals = content.find('=');
        if (equals == std::string::npos) {
            outError = "Invalid project manifest line " + std::to_string(lineNumber) +
                       ": expected key = value.";
            return false;
        }

        const std::string key = trim(std::string_view(content).substr(0, equals));
        const std::string value =
            trim(std::string_view(content).substr(equals + 1));
        std::string parseError;

        if (section == Section::ROOT) {
            if (key == "kind") {
                if (!parseQuotedString(value, outManifest.kind, parseError)) {
                    outError = parseError;
                    return false;
                }
            } else if (key == "name") {
                if (!parseQuotedString(value, outManifest.name, parseError)) {
                    outError = parseError;
                    return false;
                }
            } else if (key == "version") {
                if (!parseQuotedString(value, outManifest.version, parseError)) {
                    outError = parseError;
                    return false;
                }
            } else if (key == "description") {
                if (!parseQuotedString(value, outManifest.description, parseError)) {
                    outError = parseError;
                    return false;
                }
            } else {
                continue;
            }
            continue;
        }

        if (section == Section::WORKSPACE) {
            if (key != "members") {
                outError = "Unsupported workspace field '" + key + "'.";
                return false;
            }
            if (!parseQuotedStringArray(value, outManifest.workspaceMembers,
                                        parseError)) {
                outError = "Invalid workspace field '" + key + "': " + parseError;
                return false;
            }
            for (auto& member : outManifest.workspaceMembers) {
                member = normalizeRelativePath(member);
            }
            outManifest.workspaceMembers =
                sortedUniqueStrings(std::move(outManifest.workspaceMembers));
            continue;
        }

        if (section == Section::REGISTRY) {
            if (key != "index") {
                outError = "Unsupported registry field '" + key + "'.";
                return false;
            }

            ProjectRegistryConfig registry;
            registry.alias = currentRegistryAlias;
            if (!parseQuotedString(value, registry.index, parseError)) {
                outError = "Invalid registry field '" + key + "': " + parseError;
                return false;
            }

            auto existing = std::find_if(
                outManifest.registries.begin(), outManifest.registries.end(),
                [&](const ProjectRegistryConfig& candidate) {
                    return candidate.alias == registry.alias;
                });
            if (existing != outManifest.registries.end()) {
                *existing = std::move(registry);
            } else {
                outManifest.registries.push_back(std::move(registry));
            }
            continue;
        }

        DependencySpec dependency;
        dependency.alias = key;
        if (!parseDependencyInlineTable(value, dependency, parseError)) {
            outError = "Invalid dependency '" + key + "': " + parseError;
            return false;
        }
        if (section == Section::DEPENDENCIES) {
            outManifest.dependencies.push_back(std::move(dependency));
        } else {
            outManifest.devDependencies.push_back(std::move(dependency));
        }
    }

    if (outManifest.kind.empty()) {
        outManifest.kind = "project";
    }
    if (outManifest.kind != "project") {
        outError = "Root mog.toml must declare kind = \"project\".";
        return false;
    }

    sortRegistries(outManifest.registries);
    sortDependencies(outManifest.dependencies);
    sortDependencies(outManifest.devDependencies);
    return true;
}

bool writeProjectManifestData(const std::string& projectRoot,
                              const ProjectManifestData& manifest,
                              std::string& outError) {
    outError.clear();

    const std::filesystem::path manifestPath =
        std::filesystem::path(projectRoot) / kProjectManifestFileName;
    std::ofstream out(manifestPath);
    if (!out) {
        outError = "Could not open '" + manifestPath.string() + "' for writing.";
        return false;
    }

    out << "kind = " << quoteTomlString("project") << "\n";
    out << "name = " << quoteTomlString(manifest.name) << "\n";
    out << "version = " << quoteTomlString(manifest.version) << "\n";
    out << "description = " << quoteTomlString(manifest.description) << "\n";
    if (!manifest.workspaceMembers.empty()) {
        out << "\n[workspace]\n";
        out << "members = "
            << quoteTomlArray(sortedUniqueStrings(manifest.workspaceMembers))
            << "\n";
    }

    std::vector<ProjectRegistryConfig> registries = manifest.registries;
    std::vector<DependencySpec> dependencies = manifest.dependencies;
    std::vector<DependencySpec> devDependencies = manifest.devDependencies;
    sortRegistries(registries);
    sortDependencies(dependencies);
    sortDependencies(devDependencies);

    writeRegistryTables(out, registries);
    writeDependencyTable(out, "dependencies", dependencies);
    writeDependencyTable(out, "dev-dependencies", devDependencies);
    return true;
}

bool initializeProjectManifest(const std::string& projectRoot,
                               const std::string& projectName,
                               std::string& outError) {
    ProjectManifestData manifest;
    manifest.name = projectName.empty() ? defaultProjectName(projectRoot) : projectName;
    manifest.description = manifest.name + " project";
    return writeProjectManifestData(projectRoot, manifest, outError);
}

bool discoverDependencySpec(const std::string& projectRoot,
                            const std::string& rawSpecifier,
                            DependencySpec& outDependency,
                            std::string& outError) {
    outDependency = DependencySpec{};
    outError.clear();

    const std::string packageId = normalizePackageIdLikeSpecifier(rawSpecifier);
    ProjectManifestData manifest;
    if (!loadProjectManifestData(projectRoot, manifest, outError)) {
        return false;
    }

    std::unordered_map<std::string, PackageNode> available;
    if (!scanPackageDirectories(std::filesystem::path(projectRoot) / "packages",
                                available, outError)) {
        return false;
    }
    std::unordered_set<std::string> workspacePackageIds;
    if (!scanWorkspaceMemberPackages(std::filesystem::path(projectRoot), manifest,
                                     available, workspacePackageIds,
                                     outError)) {
        return false;
    }

    std::string requestedPackageId;
    std::string requestedVersion;
    const bool looksLikePublishedSpecifier =
        parseDependencySpecifier(rawSpecifier, requestedPackageId, requestedVersion);

    const PackageNode* matched = nullptr;
    for (const auto& [candidateId, node] : available) {
        if (node.entry.importName == rawSpecifier || candidateId == rawSpecifier ||
            (!packageId.empty() && candidateId == packageId) ||
            (looksLikePublishedSpecifier && candidateId == requestedPackageId)) {
            matched = &node;
            break;
        }
    }

    if (matched != nullptr) {
        outDependency.alias = matched->entry.importName;
        outDependency.packageId = matched->entry.packageId;
        outDependency.version = matched->entry.version;
        if (workspacePackageIds.find(matched->entry.packageId) !=
            workspacePackageIds.end()) {
            outDependency.workspace = true;
        } else {
            relativePathString(projectRoot, matched->packageDir, outDependency.path);
        }
        return true;
    }

    if (!looksLikePublishedSpecifier) {
        outError = "Cannot discover a package matching '" + rawSpecifier + "'.";
        return false;
    }

    const std::string registryAlias = defaultPublishedRegistryAlias(manifest);
    if (registryAlias.empty()) {
        outError =
            "Published package specs require a configured registry (preferably [registries.default]).";
        return false;
    }

    std::unordered_map<std::string, LoadedRegistryIndex> loadedRegistryIndexes;
    std::unordered_map<std::string, PackageNode> resolvedRegistryNodes;
    PackageNode publishedNode;
    if (!resolveRegistryPackageNode(projectRoot, manifest, registryAlias,
                                    requestedPackageId, requestedVersion,
                                    loadedRegistryIndexes, resolvedRegistryNodes,
                                    nullptr,
                                    publishedNode, outError)) {
        return false;
    }

    outDependency.alias = publishedNode.entry.importName;
    outDependency.packageId = publishedNode.entry.packageId;
    outDependency.version =
        requestedVersion.empty() ? publishedNode.entry.version : requestedVersion;
    if (registryAlias != "default") {
        outDependency.registry = registryAlias;
    }
    return true;
}

bool addProjectDependency(const std::string& projectRoot,
                          const DependencySpec& dependency,
                          std::string& outError) {
    ProjectManifestData manifest;
    if (!loadProjectManifestData(projectRoot, manifest, outError)) {
        return false;
    }

    auto existing = std::find_if(
        manifest.dependencies.begin(), manifest.dependencies.end(),
        [&](const DependencySpec& candidate) {
            return candidate.alias == dependency.alias;
        });
    if (existing != manifest.dependencies.end()) {
        *existing = dependency;
    } else {
        manifest.dependencies.push_back(dependency);
    }

    sortDependencies(manifest.dependencies);
    return writeProjectManifestData(projectRoot, manifest, outError);
}

bool publishProjectPackage(const std::string& projectRoot,
                           const std::string& packageDir,
                           const std::string& registryAlias,
                           std::string& outError) {
    ProjectManifestData projectManifest;
    if (!loadProjectManifestData(projectRoot, projectManifest, outError)) {
        return false;
    }

    const std::string effectiveRegistryAlias =
        registryAlias.empty() ? defaultPublishedRegistryAlias(projectManifest)
                              : registryAlias;
    if (effectiveRegistryAlias.empty()) {
        outError =
            "Publish requires a configured registry alias (preferably [registries.default]).";
        return false;
    }

    const ProjectRegistryConfig* registry =
        findRegistryConfig(projectManifest, effectiveRegistryAlias);
    if (registry == nullptr) {
        outError = "Unknown registry '" + effectiveRegistryAlias + "'.";
        return false;
    }

    const std::filesystem::path packagePath = canonicalOrLexical(packageDir);
    PackageManifest manifest;
    if (!loadPackageManifest(packagePath.string(), manifest, outError)) {
        return false;
    }

    for (const auto& dependency : manifest.dependencies) {
        if (dependency.packageId.empty() || !dependency.path.empty() ||
            dependency.workspace || !dependency.git.empty()) {
            outError = "Published packages currently require published dependency specs with package = \"namespace:name\" and optional version constraints.";
            return false;
        }
    }

    PackageRegistryEntry packageEntry;
    if (!loadPackageEntryFromDir(packagePath, packageEntry, nullptr, outError)) {
        return false;
    }
    ensureResolvedLibraryPath(packageEntry, packagePath, projectRoot);
    if (!validatePackageEntry(packageEntry, projectRoot, outError)) {
        return false;
    }

    ProjectManifestData resolverManifest;
    resolverManifest.registries = projectManifest.registries;
    resolverManifest.dependencies = manifest.dependencies;
    InstallOptions resolveOptions;
    resolveOptions.includeDevDependencies = false;
    resolveOptions.update = true;

    std::vector<PackageRegistryEntry> resolvedDependencies;
    if (!collectResolvedPackages(projectRoot, resolverManifest, resolveOptions,
                                 resolvedDependencies, outError)) {
        return false;
    }

    std::vector<RegistryDependencyPin> dependencyPins;
    for (const auto& dependency : manifest.dependencies) {
        auto it = std::find_if(
            resolvedDependencies.begin(), resolvedDependencies.end(),
            [&](const PackageRegistryEntry& entry) {
                return entry.packageId == dependency.packageId;
            });
        if (it == resolvedDependencies.end()) {
            outError = "Could not resolve dependency '" + dependency.packageId +
                       "' for publish.";
            return false;
        }
        dependencyPins.push_back(RegistryDependencyPin{it->packageId, it->version});
    }
    std::sort(dependencyPins.begin(), dependencyPins.end(),
              [](const RegistryDependencyPin& lhs,
                 const RegistryDependencyPin& rhs) {
                  if (lhs.packageId != rhs.packageId) {
                      return lhs.packageId < rhs.packageId;
                  }
                  return lhs.version < rhs.version;
              });

    std::filesystem::path indexPath;
    std::filesystem::path registryRoot;
    if (!resolveRegistryIndexLocation(projectRoot, *registry, indexPath, registryRoot,
                                      outError)) {
        return false;
    }

    LoadedRegistryIndex existingIndex;
    std::vector<RegistryIndexRecord> records;
    if (fileExists(indexPath)) {
        if (!loadRegistryIndex(projectRoot, *registry, existingIndex, outError)) {
            return false;
        }
        for (const auto& [key, record] : existingIndex.recordsByKey) {
            records.push_back(record);
        }
    }

    const std::string publishedTarget =
        packageEntry.kind == "native" ? detectHostTarget() : "";
    const std::filesystem::path sourceArtifactDir =
        packageEntry.kind == "native"
            ? registryRoot / "packages" / packageEntry.packageNamespace /
                  packageEntry.packageName / packageEntry.version / "source"
            : registryRoot / "packages" / packageEntry.packageNamespace /
                  packageEntry.packageName / packageEntry.version;
    const std::string relativeSourceArtifactPath =
        normalizeRelativePath(sourceArtifactDir.lexically_relative(registryRoot)
                                  .lexically_normal()
                                  .string());
    const std::filesystem::path nativeArtifactDir =
        registryRoot / "packages" / packageEntry.packageNamespace /
        packageEntry.packageName / packageEntry.version / publishedTarget;
    const std::string relativeNativeArtifactPath =
        normalizeRelativePath(nativeArtifactDir.lexically_relative(registryRoot)
                                  .lexically_normal()
                                  .string());

    std::filesystem::path sourceArtifactSource = packagePath;
    std::filesystem::path nativeArtifactSource;
    ScopedPathCleanup stagedSourceArtifactCleanup;
    ScopedPathCleanup stagedNativeArtifactCleanup;
    if (packageEntry.kind == "native") {
        if (!stagePublishedNativeSourceArtifact(packagePath, sourceArtifactSource,
                                                outError)) {
            return false;
        }
        stagedSourceArtifactCleanup.path = sourceArtifactSource;
        if (!stagePublishedNativePrebuiltArtifact(packagePath, manifest, packageEntry,
                                                  nativeArtifactSource,
                                                  outError)) {
            return false;
        }
        stagedNativeArtifactCleanup.path = nativeArtifactSource;
    }

    const std::string sourceArtifactDigest = digestDirectory(sourceArtifactSource);
    if (sourceArtifactDigest.empty()) {
        outError = "Could not compute publish artifact digest for '" +
                   sourceArtifactSource.string() + "'.";
        return false;
    }
    std::string nativeArtifactDigest;
    if (packageEntry.kind == "native") {
        nativeArtifactDigest = digestDirectory(nativeArtifactSource);
        if (nativeArtifactDigest.empty()) {
            outError = "Could not compute publish artifact digest for '" +
                       nativeArtifactSource.string() + "'.";
            return false;
        }
    }

    const std::string recordKey =
        registryRecordKey(packageEntry.packageId, packageEntry.version);
    auto existingRecordIt =
        std::find_if(records.begin(), records.end(),
                     [&](const RegistryIndexRecord& record) {
                         return registryRecordKey(record.packageId, record.version) ==
                                recordKey;
                     });
    if (existingRecordIt != records.end()) {
        const bool sameDependencies =
            existingRecordIt->dependencies.size() == dependencyPins.size() &&
            std::equal(existingRecordIt->dependencies.begin(),
                       existingRecordIt->dependencies.end(),
                       dependencyPins.begin(),
                       [](const RegistryDependencyPin& lhs,
                          const RegistryDependencyPin& rhs) {
                           return lhs.packageId == rhs.packageId &&
                                  lhs.version == rhs.version;
                       });
        if (!sameDependencies) {
            outError = "Registry already contains '" + packageEntry.packageId +
                       "@" + packageEntry.version +
                       "' with different published contents.";
            return false;
        }

        if (packageEntry.kind == "native") {
            if ((!existingRecordIt->artifactPath.empty() ||
                 !existingRecordIt->artifactDigest.empty()) &&
                (existingRecordIt->artifactDigest != sourceArtifactDigest ||
                 existingRecordIt->artifactPath != relativeSourceArtifactPath)) {
                outError = "Registry already contains '" + packageEntry.packageId +
                           "@" + packageEntry.version +
                           "' with different published contents.";
                return false;
            }
            auto targetIt = std::find(existingRecordIt->nativeTargets.begin(),
                                      existingRecordIt->nativeTargets.end(),
                                      publishedTarget);
            if (targetIt != existingRecordIt->nativeTargets.end()) {
                const size_t targetIndex = static_cast<size_t>(
                    std::distance(existingRecordIt->nativeTargets.begin(),
                                  targetIt));
                if (existingRecordIt->nativeArtifactDigests[targetIndex] !=
                        nativeArtifactDigest ||
                    existingRecordIt->nativeArtifactPaths[targetIndex] !=
                        relativeNativeArtifactPath) {
                    outError = "Registry already contains '" +
                               packageEntry.packageId + "@" +
                               packageEntry.version + "' for target '" +
                               publishedTarget +
                               "' with different published contents.";
                    return false;
                }
            }
        } else if (existingRecordIt->artifactDigest != sourceArtifactDigest ||
                   existingRecordIt->artifactPath != relativeSourceArtifactPath) {
            outError = "Registry already contains '" + packageEntry.packageId +
                       "@" + packageEntry.version +
                       "' with different published contents.";
            return false;
        }
    }

    if (fileExists(sourceArtifactDir)) {
        const std::string existingDigest = digestDirectory(sourceArtifactDir);
        if (!existingDigest.empty() && existingDigest != sourceArtifactDigest) {
            outError = "Registry artifact path '" + sourceArtifactDir.string() +
                       "' already exists with different contents.";
            return false;
        }
    } else {
        if (!copyDirectoryRecursive(sourceArtifactSource, sourceArtifactDir,
                                   outError)) {
            return false;
        }
    }
    if (packageEntry.kind == "native") {
        if (fileExists(nativeArtifactDir)) {
            const std::string existingDigest = digestDirectory(nativeArtifactDir);
            if (!existingDigest.empty() && existingDigest != nativeArtifactDigest) {
                outError = "Registry artifact path '" + nativeArtifactDir.string() +
                           "' already exists with different contents.";
                return false;
            }
        } else if (!copyDirectoryRecursive(nativeArtifactSource, nativeArtifactDir,
                                           outError)) {
            return false;
        }
    }

    if (existingRecordIt == records.end()) {
        RegistryIndexRecord record;
        record.packageId = packageEntry.packageId;
        record.version = packageEntry.version;
        record.parsedVersion = SemanticVersion{};
        parseSemanticVersion(record.version, record.parsedVersion);
        if (packageEntry.kind == "native") {
            record.artifactPath = relativeSourceArtifactPath;
            record.artifactDigest = sourceArtifactDigest;
            record.nativeTargets.push_back(publishedTarget);
            record.nativeArtifactPaths.push_back(relativeNativeArtifactPath);
            record.nativeArtifactDigests.push_back(nativeArtifactDigest);
        } else {
            record.artifactPath = relativeSourceArtifactPath;
            record.artifactDigest = sourceArtifactDigest;
        }
        record.dependencies = dependencyPins;
        records.push_back(std::move(record));
    } else if (packageEntry.kind == "native") {
        existingRecordIt->artifactPath = relativeSourceArtifactPath;
        existingRecordIt->artifactDigest = sourceArtifactDigest;
        auto targetIt = std::find(existingRecordIt->nativeTargets.begin(),
                                  existingRecordIt->nativeTargets.end(),
                                  publishedTarget);
        if (targetIt == existingRecordIt->nativeTargets.end()) {
            existingRecordIt->nativeTargets.push_back(publishedTarget);
            existingRecordIt->nativeArtifactPaths.push_back(relativeNativeArtifactPath);
            existingRecordIt->nativeArtifactDigests.push_back(nativeArtifactDigest);
        }
    }

    return writeRegistryIndexFile(indexPath, std::move(records), outError);
}

bool installProjectPackages(const std::string& projectRoot,
                            std::vector<PackageRegistryEntry>& outEntries,
                            const InstallOptions& options,
                            std::string& outError) {
    ProjectManifestData manifest;
    if (!loadProjectManifestData(projectRoot, manifest, outError)) {
        return false;
    }

    std::vector<PackageRegistryEntry> resolvedEntries;
    bool usingLockfile = false;
    if (!options.update) {
        std::vector<PackageRegistryEntry> lockedEntries;
        std::string lockError;
        if (loadProjectLockfile(projectRoot, lockedEntries, lockError)) {
            if (validateLockedEntriesAgainstManifest(projectRoot, manifest, options,
                                                    lockedEntries, lockError)) {
                resolvedEntries = std::move(lockedEntries);
                usingLockfile = true;
            } else if (options.locked) {
                outError = lockError;
                return false;
            }
        } else if (options.locked) {
            outError = lockError;
            return false;
        }
    }

    if (!usingLockfile) {
        if (!collectResolvedPackages(projectRoot, manifest, options, resolvedEntries,
                                     outError)) {
            return false;
        }

        if (options.locked) {
            std::vector<PackageRegistryEntry> lockedEntries;
            if (!loadProjectLockfile(projectRoot, lockedEntries, outError)) {
                return false;
            }
            if (!validateLockedGraph(resolvedEntries, lockedEntries, outError)) {
                return false;
            }
        }
    }

    if (!materializeInstalledPackages(projectRoot, resolvedEntries, options,
                                      outEntries, outError)) {
        return false;
    }

    const std::filesystem::path projectRootPath(projectRoot);
    const std::filesystem::path registryPath = installRegistryPath(projectRootPath);
    if (!writeInstallRegistryFile(registryPath, projectRootPath, outEntries,
                                  outError)) {
        return false;
    }

    if (!options.locked && !usingLockfile) {
        const std::filesystem::path lockfilePath = projectRootPath / kLockfileName;
        if (!writeLockfile(lockfilePath, projectRootPath, resolvedEntries, outError)) {
            return false;
        }
    }

    std::error_code ec;
    std::filesystem::create_directories(preferredLocalCacheRoot(projectRootPath),
                                        ec);
    return true;
}

bool ensureProjectPackagesInstalled(const std::string& projectRoot,
                                    const InstallOptions& options,
                                    std::string& outError) {
    outError.clear();

    ProjectManifestData manifest;
    if (!loadProjectManifestData(projectRoot, manifest, outError)) {
        return false;
    }

    if (manifest.dependencies.empty() &&
        (!options.includeDevDependencies || manifest.devDependencies.empty())) {
        return true;
    }

    const std::filesystem::path registryPath =
        installRegistryPath(std::filesystem::path(projectRoot));
    const std::filesystem::path manifestPath =
        std::filesystem::path(projectRoot) / kProjectManifestFileName;

    std::error_code ec;
    if (!options.locked && std::filesystem::exists(registryPath, ec) && !ec) {
        std::vector<PackageRegistryEntry> installedEntries;
        std::string registryError;
        bool targetMismatch = false;
        if (loadProjectPackageRegistry(projectRoot, installedEntries, registryError)) {
            const std::string desiredTarget = effectiveInstallTarget(&options);
            for (const auto& entry : installedEntries) {
                if (entry.kind != "native" || entry.sourceType != "registry") {
                    continue;
                }
                if (entry.selectedTarget != desiredTarget) {
                    targetMismatch = true;
                    break;
                }
            }
        }

        const auto registryTime = std::filesystem::last_write_time(registryPath, ec);
        if (!ec && !targetMismatch) {
            const auto manifestTime =
                std::filesystem::last_write_time(manifestPath, ec);
            if (!ec && registryTime >= manifestTime) {
                return true;
            }
        }
    }

    std::vector<PackageRegistryEntry> installedEntries;
    return installProjectPackages(projectRoot, installedEntries, options, outError);
}
