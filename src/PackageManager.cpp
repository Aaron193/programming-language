bool isExactPublishedVersion(std::string_view version) {
    if (version.empty()) {
        return false;
    }

    size_t componentCount = 0;
    size_t componentLength = 0;
    for (char ch : version) {
        if (ch == '.') {
            if (componentLength == 0) {
                return false;
            }
            ++componentCount;
            componentLength = 0;
            continue;
        }
        if (!std::isdigit(static_cast<unsigned char>(ch))) {
            return false;
        }
        ++componentLength;
    }

    return componentLength > 0 && componentCount == 2;
}

bool parseRegistryDependencyPin(std::string_view text,
                                RegistryDependencyPin& outPin,
                                std::string& outError) {
    outPin = RegistryDependencyPin{};
    const size_t at = text.rfind('@');
    if (at == std::string_view::npos || at == 0 || at + 1 >= text.size()) {
        outError = "Registry dependency pins must use 'namespace:name@x.y.z'.";
        return false;
    }

    outPin.packageId = std::string(text.substr(0, at));
    outPin.version = std::string(text.substr(at + 1));
    if (!isExactPublishedVersion(outPin.version)) {
        outError = "Registry dependency pin '" + std::string(text) +
                   "' must use an exact x.y.z version.";
        return false;
    }
    return true;
}

bool parseDependencyInlineTable(const std::string& value,
                                ProjectDependencySpec& outDependency,
                                std::string& outError) {
    if (value.size() < 2 || value.front() != '{' || value.back() != '}') {
        outError = "Dependency entries must be inline tables.";
        return false;
    }

    std::string body = trim(std::string_view(value).substr(1, value.size() - 2));
    while (!body.empty()) {
        const size_t equals = body.find('=');
        if (equals == std::string::npos) {
            outError = "Dependency table must use key = value entries.";
            return false;
        }

        const std::string key = trim(std::string_view(body).substr(0, equals));
        body = trim(std::string_view(body).substr(equals + 1));
        if (body.empty()) {
            outError = "Dependency table entry is missing a value.";
            return false;
        }

        size_t valueLength = 0;
        bool inString = false;
        bool escaped = false;
        for (; valueLength < body.size(); ++valueLength) {
            const char ch = body[valueLength];
            if (inString) {
                if (escaped) {
                    escaped = false;
                } else if (ch == '\\') {
                    escaped = true;
                } else if (ch == '"') {
                    inString = false;
                }
                continue;
            }

            if (ch == '"') {
                inString = true;
                continue;
            }

            if (ch == ',') {
                break;
            }
        }

        const std::string rawValue = trim(body.substr(0, valueLength));
        if (key == "path") {
            std::string parsed;
            if (!parseQuotedString(rawValue, parsed, outError)) {
                return false;
            }
            outDependency.path = normalizeRelativePath(parsed);
        } else if (key == "package") {
            std::string parsed;
            if (!parseQuotedString(rawValue, parsed, outError)) {
                return false;
            }
            outDependency.packageId = parsed;
        } else if (key == "version") {
            std::string parsed;
            if (!parseQuotedString(rawValue, parsed, outError)) {
                return false;
            }
            outDependency.version = parsed;
        } else if (key == "git") {
            std::string parsed;
            if (!parseQuotedString(rawValue, parsed, outError)) {
                return false;
            }
            outDependency.git = parsed;
        } else if (key == "registry") {
            std::string parsed;
            if (!parseQuotedString(rawValue, parsed, outError)) {
                return false;
            }
            outDependency.registry = parsed;
        } else if (key == "workspace") {
            if (!parseBoolValue(rawValue, outDependency.workspace, outError)) {
                return false;
            }
        } else {
            outError = "Unsupported dependency field '" + key + "'.";
            return false;
        }

        if (valueLength >= body.size()) {
            body.clear();
        } else {
            body = trim(std::string_view(body).substr(valueLength + 1));
        }
    }

    if (outDependency.path.empty() && !outDependency.workspace &&
        outDependency.git.empty() && outDependency.registry.empty() &&
        outDependency.packageId.empty()) {
        outError = "Dependency entries must define path, workspace, git, registry, or package metadata.";
        return false;
    }

    return true;
}

std::string readFileText(const std::filesystem::path& path) {
    std::ifstream file(path, std::ios::binary);
    if (!file) {
        return "";
    }
    return std::string((std::istreambuf_iterator<char>(file)),
                       std::istreambuf_iterator<char>());
}

std::string hashString(std::string_view text) {
    uint64_t hash = 1469598103934665603ull;
    for (unsigned char ch : text) {
        hash ^= static_cast<uint64_t>(ch);
        hash *= 1099511628211ull;
    }

    std::ostringstream out;
    out << std::hex << std::setfill('0') << std::setw(16) << hash;
    return out.str();
}

std::string digestFile(const std::filesystem::path& path) {
    return hashString(readFileText(path));
}

std::string manifestDigestForDir(const std::filesystem::path& packageDir) {
    const std::filesystem::path mogManifest = packageDir / kProjectManifestFileName;
    if (fileExists(mogManifest)) {
        return digestFile(mogManifest);
    }

    const std::filesystem::path legacyManifest = packageDir / "package.toml";
    if (fileExists(legacyManifest)) {
        return digestFile(legacyManifest);
    }

    return "";
}

std::string digestDirectory(const std::filesystem::path& root) {
    std::error_code ec;
    if (!std::filesystem::exists(root, ec) || ec) {
        return "";
    }

    std::vector<std::filesystem::path> files;
    for (std::filesystem::recursive_directory_iterator it(root, ec), end;
         !ec && it != end; it.increment(ec)) {
        if (!it->is_regular_file(ec)) {
            continue;
        }
        files.push_back(it->path());
    }
    if (ec) {
        return "";
    }

    std::sort(files.begin(), files.end());

    std::string seed;
    for (const auto& file : files) {
        const std::filesystem::path relative = file.lexically_relative(root);
        seed += normalizeRelativePath(relative.string());
        seed += '\n';
        seed += readFileText(file);
        seed += '\n';
    }

    return hashString(seed);
}

std::vector<std::string> sortedUniqueStrings(std::vector<std::string> values) {
    std::sort(values.begin(), values.end());
    values.erase(std::unique(values.begin(), values.end()), values.end());
    return values;
}

bool removePathIfExists(const std::filesystem::path& path, std::string& outError) {
    std::error_code ec;
    if (!std::filesystem::exists(path, ec) && !std::filesystem::is_symlink(path, ec)) {
        return true;
    }
    std::filesystem::remove_all(path, ec);
    if (ec) {
        outError = "Could not remove '" + path.string() + "'.";
        return false;
    }
    return true;
}

bool ensureParentDirectory(const std::filesystem::path& path, std::string& outError) {
    std::error_code ec;
    std::filesystem::create_directories(path.parent_path(), ec);
    if (ec) {
        outError = "Could not create directory '" + path.parent_path().string() + "'.";
        return false;
    }
    return true;
}

bool copyFileReplacing(const std::filesystem::path& from,
                       const std::filesystem::path& to,
                       std::string& outError) {
    if (!ensureParentDirectory(to, outError)) {
        return false;
    }

    std::error_code ec;
    std::filesystem::copy_file(from, to,
                               std::filesystem::copy_options::overwrite_existing,
                               ec);
    if (ec) {
        outError = "Could not copy '" + from.string() + "' to '" + to.string() + "'.";
        return false;
    }
    return true;
}

bool copyDirectoryRecursive(const std::filesystem::path& from,
                            const std::filesystem::path& to,
                            std::string& outError) {
    std::error_code ec;
    std::filesystem::create_directories(to, ec);
    if (ec) {
        outError = "Could not create directory '" + to.string() + "'.";
        return false;
    }

    std::filesystem::recursive_directory_iterator it(from, ec), end;
    for (; !ec && it != end; it.increment(ec)) {
        const std::filesystem::path relative = it->path().lexically_relative(from);
        const std::filesystem::path destination = to / relative;
        if (it->is_directory(ec)) {
            std::filesystem::create_directories(destination, ec);
            if (ec) {
                outError = "Could not create directory '" + destination.string() + "'.";
                return false;
            }
            continue;
        }

        if (!it->is_regular_file(ec)) {
            continue;
        }

        if (!copyFileReplacing(it->path(), destination, outError)) {
            return false;
        }
    }

    if (ec) {
        outError = "Could not copy directory '" + from.string() + "'.";
        return false;
    }

    return true;
}

