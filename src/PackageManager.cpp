std::filesystem::path userCacheRoot() {
    if (const char* cacheDir = std::getenv("MOG_CACHE_DIR");
        cacheDir != nullptr && *cacheDir != '\0') {
        return std::filesystem::path(cacheDir) / "packages";
    }

    if (const char* xdgCache = std::getenv("XDG_CACHE_HOME");
        xdgCache != nullptr && *xdgCache != '\0') {
        return std::filesystem::path(xdgCache) / "mog" / "packages";
    }

    if (const char* home = std::getenv("HOME"); home != nullptr && *home != '\0') {
        return std::filesystem::path(home) / ".cache" / "mog" / "packages";
    }

    return std::filesystem::temp_directory_path() / "mog-cache" / "packages";
}

std::filesystem::path localCacheRoot() {
    return userCacheRoot() / "local";
}

std::filesystem::path projectFallbackCacheRoot(
    const std::filesystem::path& projectRoot) {
    return projectRoot / ".mog" / "cache" / "packages" / "local";
}

std::filesystem::path preferredLocalCacheRoot(
    const std::filesystem::path& projectRoot) {
    const std::filesystem::path preferred = localCacheRoot();
    std::error_code ec;
    std::filesystem::create_directories(preferred, ec);
    if (!ec) {
        return preferred;
    }
    return projectFallbackCacheRoot(projectRoot);
}

