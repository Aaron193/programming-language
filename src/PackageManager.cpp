bool loadCacheMetadataFile(const std::filesystem::path& metadataPath,
                           CacheEntryMetadata& outMetadata,
                           std::string& outError) {
    outMetadata = CacheEntryMetadata{};
    std::ifstream file(metadataPath);
    if (!file) {
        outError = "Could not open cache metadata '" + metadataPath.string() + "'.";
        return false;
    }

    std::string line;
    size_t lineNumber = 0;
    while (std::getline(file, line)) {
        ++lineNumber;
        const std::string content = stripComment(line);
        if (content.empty()) {
            continue;
        }

        const size_t equals = content.find('=');
        if (equals == std::string::npos) {
            outError = "Invalid cache metadata line " + std::to_string(lineNumber) +
                       ": expected key = value.";
            return false;
        }

        const std::string key = trim(std::string_view(content).substr(0, equals));
        const std::string value =
            trim(std::string_view(content).substr(equals + 1));

        if (key == "dependency_groups") {
            if (!parseQuotedStringArray(value, outMetadata.dependencyGroups,
                                        outError)) {
                outError = "Invalid cache metadata line " +
                           std::to_string(lineNumber) + ": " + outError;
                return false;
            }
            continue;
        }
        if (key == "build_from_source") {
            if (!parseBoolValue(value, outMetadata.buildFromSource, outError)) {
                outError = "Invalid cache metadata line " +
                           std::to_string(lineNumber) + ": " + outError;
                return false;
            }
            continue;
        }

        std::string parsed;
        if (!parseQuotedString(value, parsed, outError)) {
            outError = "Invalid cache metadata line " +
                       std::to_string(lineNumber) + ": " + outError;
            return false;
        }

        if (key == "package_id") {
            outMetadata.packageId = parsed;
        } else if (key == "version") {
            outMetadata.version = parsed;
        } else if (key == "kind") {
            outMetadata.kind = parsed;
        } else if (key == "source_type") {
            outMetadata.sourceType = parsed;
        } else if (key == "source_path") {
            outMetadata.sourcePath = parsed;
        } else if (key == "registry") {
            outMetadata.registry = parsed;
        } else if (key == "artifact_path") {
            outMetadata.artifactPath = parsed;
        } else if (key == "artifact_digest") {
            outMetadata.artifactDigest = parsed;
        } else if (key == "selected_target") {
            outMetadata.selectedTarget = parsed;
        } else if (key == "manifest_digest") {
            outMetadata.manifestDigest = parsed;
        } else if (key == "api_digest") {
            outMetadata.apiDigest = parsed;
        } else {
            outError = "Unsupported cache metadata field '" + key + "'.";
            return false;
        }
    }

    outMetadata.dependencyGroups =
        sortedUniqueStrings(std::move(outMetadata.dependencyGroups));
    return true;
}

bool cacheMetadataMatchesEntry(const CacheEntryMetadata& metadata,
                               const PackageRegistryEntry& entry) {
    return metadata.packageId == entry.packageId &&
           metadata.version == entry.version && metadata.kind == entry.kind &&
           metadata.sourceType == entry.sourceType &&
           metadata.sourcePath == entry.sourcePath &&
           metadata.registry == entry.registry &&
           metadata.artifactPath == entry.artifactPath &&
           metadata.artifactDigest == entry.artifactDigest &&
           metadata.selectedTarget == entry.selectedTarget &&
           metadata.buildFromSource == entry.buildFromSource &&
           metadata.manifestDigest == entry.manifestDigest &&
           metadata.apiDigest == entry.apiDigest &&
           metadata.dependencyGroups ==
               sortedUniqueStrings(entry.dependencyGroups);
}

bool addAvailableNode(std::unordered_map<std::string, PackageNode>& outNodes,
                      PackageNode node, std::string& outError) {
    auto existing = outNodes.find(node.entry.packageId);
    if (existing != outNodes.end()) {
        if (existing->second.packageDir != node.packageDir) {
            outError = "Duplicate local package '" + node.entry.packageId +
                       "' found at '" + existing->second.packageDir.string() +
                       "' and '" + node.packageDir.string() + "'.";
            return false;
        }
        return true;
    }

    outNodes[node.entry.packageId] = std::move(node);
    return true;
}

bool resolveManifestPath(const std::string& projectRoot,
                         const std::string& rawPath,
                         std::filesystem::path& outPath,
                         std::string& outError) {
    outError.clear();
    if (rawPath.rfind("file://", 0) == 0) {
        outPath = std::filesystem::path(rawPath.substr(7));
        return true;
    }

    const std::filesystem::path candidate(rawPath);
    if (candidate.is_absolute()) {
        outPath = candidate;
        return true;
    }

    if (rawPath.empty()) {
        outError = "Registry index path cannot be empty.";
        return false;
    }

    outPath = std::filesystem::path(projectRoot) / candidate;
    return true;
}

bool loadRegistryIndex(const std::string& projectRoot,
                       const ProjectRegistryConfig& config,
                       LoadedRegistryIndex& outIndex,
                       std::string& outError) {
    outIndex = LoadedRegistryIndex{};
    outError.clear();

    std::filesystem::path configuredPath;
    if (!resolveManifestPath(projectRoot, config.index, configuredPath, outError)) {
        return false;
    }

    std::error_code ec;
    if (std::filesystem::is_directory(configuredPath, ec) && !ec) {
        configuredPath /= "index.toml";
    }

    configuredPath = std::filesystem::weakly_canonical(configuredPath, ec);
    if (ec || !std::filesystem::exists(configuredPath, ec) || ec) {
        outError = "Registry '" + config.alias + "' index does not exist at '" +
                   configuredPath.lexically_normal().string() + "'.";
        return false;
    }

    outIndex.indexPath = configuredPath;
    outIndex.rootDir = configuredPath.parent_path();

    std::ifstream file(configuredPath);
    if (!file) {
        outError = "Could not open registry index '" + configuredPath.string() + "'.";
        return false;
    }

    RegistryIndexRecord current;
    bool hasCurrent = false;
    std::string line;
    size_t lineNumber = 0;

    auto flushCurrent = [&]() -> bool {
        if (!hasCurrent) {
            return true;
        }
        const bool hasPrimaryArtifact =
            !current.artifactPath.empty() || !current.artifactDigest.empty();
        const bool hasNativeArtifacts = !current.nativeTargets.empty() ||
                                       !current.nativeArtifactPaths.empty() ||
                                       !current.nativeArtifactDigests.empty();
        if (current.packageId.empty() || current.version.empty() ||
            (!hasPrimaryArtifact && !hasNativeArtifacts)) {
            outError = "Registry '" + config.alias +
                       "' index entries must define package_id, version, and at least one artifact.";
            return false;
        }
        if ((!current.artifactPath.empty() && current.artifactDigest.empty()) ||
            (current.artifactPath.empty() && !current.artifactDigest.empty())) {
            outError = "Registry '" + config.alias +
                       "' source artifact entries must define both artifact_path and artifact_digest.";
            return false;
        }
        if (current.nativeTargets.size() != current.nativeArtifactPaths.size() ||
            current.nativeTargets.size() != current.nativeArtifactDigests.size()) {
            outError = "Registry '" + config.alias +
                       "' native artifact fields must define the same number of targets, paths, and digests.";
            return false;
        }
        if (!isExactPublishedVersion(current.version)) {
            outError = "Registry '" + config.alias + "' package '" +
                       current.packageId + "' must use an exact x.y.z version.";
            return false;
        }
        if (!parseSemanticVersion(current.version, current.parsedVersion)) {
            outError = "Registry '" + config.alias + "' package '" +
                       current.packageId + "' must use an exact x.y.z version.";
            return false;
        }
        const std::string key = registryRecordKey(current.packageId, current.version);
        if (!outIndex.recordsByKey.emplace(key, current).second) {
            outError = "Registry '" + config.alias + "' contains a duplicate entry for '" +
                       key + "'.";
            return false;
        }
        outIndex.recordsByPackageId[current.packageId].push_back(current);
        current = RegistryIndexRecord{};
        hasCurrent = false;
        return true;
    };

    while (std::getline(file, line)) {
        ++lineNumber;
        const std::string content = stripComment(line);
        if (content.empty()) {
            continue;
        }

        if (content == "[[package]]") {
            if (!flushCurrent()) {
                return false;
            }
            hasCurrent = true;
            continue;
        }

        const size_t equals = content.find('=');
        if (equals == std::string::npos) {
            outError = "Invalid registry index line " + std::to_string(lineNumber) +
                       ": expected key = value.";
            return false;
        }

        const std::string key = trim(std::string_view(content).substr(0, equals));
        const std::string value =
            trim(std::string_view(content).substr(equals + 1));

        if (!hasCurrent) {
            if (key == "schema_version") {
                continue;
            }
            outError = "Invalid registry index line " + std::to_string(lineNumber) +
                       ": entries must appear inside [[package]].";
            return false;
        }

        if (key == "dependencies") {
            std::vector<std::string> pins;
            if (!parseQuotedStringArray(value, pins, outError)) {
                outError = "Invalid registry index line " +
                           std::to_string(lineNumber) + ": " + outError;
                return false;
            }
            current.dependencies.clear();
            for (const std::string& pinText : pins) {
                RegistryDependencyPin pin;
                if (!parseRegistryDependencyPin(pinText, pin, outError)) {
                    outError = "Invalid registry index line " +
                               std::to_string(lineNumber) + ": " + outError;
                    return false;
                }
                current.dependencies.push_back(std::move(pin));
            }
            continue;
        }

        if (key == "native_targets") {
            if (!parseQuotedStringArray(value, current.nativeTargets, outError)) {
                outError = "Invalid registry index line " +
                           std::to_string(lineNumber) + ": " + outError;
                return false;
            }
            continue;
        }
        if (key == "native_artifact_paths") {
            if (!parseQuotedStringArray(value, current.nativeArtifactPaths,
                                        outError)) {
                outError = "Invalid registry index line " +
                           std::to_string(lineNumber) + ": " + outError;
                return false;
            }
            continue;
        }
        if (key == "native_artifact_digests") {
            if (!parseQuotedStringArray(value, current.nativeArtifactDigests,
                                        outError)) {
                outError = "Invalid registry index line " +
                           std::to_string(lineNumber) + ": " + outError;
                return false;
            }
            continue;
        }

        std::string parsed;
        if (!parseQuotedString(value, parsed, outError)) {
            outError = "Invalid registry index line " + std::to_string(lineNumber) +
                       ": " + outError;
            return false;
        }

        if (key == "package_id") {
            current.packageId = parsed;
        } else if (key == "version") {
            current.version = parsed;
        } else if (key == "artifact_path") {
            current.artifactPath = parsed;
        } else if (key == "artifact_digest") {
            current.artifactDigest = parsed;
        } else if (key == "schema_version") {
            continue;
        } else {
            outError = "Unsupported registry index field '" + key + "'.";
            return false;
        }
    }

    if (!flushCurrent()) {
        return false;
    }

    for (auto& [packageId, records] : outIndex.recordsByPackageId) {
        std::sort(records.begin(), records.end(),
                  [](const RegistryIndexRecord& lhs,
                     const RegistryIndexRecord& rhs) {
                      return compareSemanticVersions(lhs.parsedVersion,
                                                     rhs.parsedVersion) > 0;
                  });
    }

    return true;
}

bool resolveRegistryIndexLocation(const std::string& projectRoot,
                                  const ProjectRegistryConfig& config,
                                  std::filesystem::path& outIndexPath,
                                  std::filesystem::path& outRootDir,
                                  std::string& outError) {
    std::filesystem::path configuredPath;
    if (!resolveManifestPath(projectRoot, config.index, configuredPath, outError)) {
        return false;
    }

    std::error_code ec;
    if (std::filesystem::is_directory(configuredPath, ec) && !ec) {
        configuredPath /= "index.toml";
    } else if (!configuredPath.has_filename() ||
               configuredPath.filename() != "index.toml") {
        configuredPath /= "index.toml";
    }

    outIndexPath = configuredPath.lexically_normal();
    outRootDir = outIndexPath.parent_path();
    return true;
}

bool scanPackageDirectories(const std::filesystem::path& root,
                            std::unordered_map<std::string, PackageNode>& outNodes,
                            std::string& outError) {
    std::error_code ec;
    if (!std::filesystem::exists(root, ec) || ec) {
        return true;
    }

    for (std::filesystem::recursive_directory_iterator it(root, ec), end;
         !ec && it != end; it.increment(ec)) {
        if (ec || !it->is_directory()) {
            continue;
        }

        const std::filesystem::path dir = it->path();
        if (!fileExists(dir / kProjectManifestFileName) &&
            !fileExists(dir / "package.toml")) {
            continue;
        }

        PackageNode node;
        if (!loadPackageEntryFromDir(dir, node.entry, nullptr, outError)) {
            continue;
        }
        node.packageDir = std::filesystem::path(node.entry.packageDir);
        if (!addAvailableNode(outNodes, std::move(node), outError)) {
            return false;
        }
    }

    return true;
}

bool scanWorkspaceMemberPackages(
    const std::filesystem::path& projectRoot, const ProjectManifestData& manifest,
    std::unordered_map<std::string, PackageNode>& outNodes,
    std::unordered_set<std::string>& outWorkspacePackageIds, std::string& outError) {
    for (const std::string& member : manifest.workspaceMembers) {
        const std::filesystem::path memberPath = projectRoot / member;
        if (!isDirectory(memberPath)) {
            outError = "Workspace member '" + member + "' does not exist.";
            return false;
        }

        PackageNode node;
        if (!loadPackageEntryFromDir(memberPath, node.entry, nullptr, outError)) {
            outError = "Could not load workspace member '" + member + "': " +
                       outError;
            return false;
        }

        node.packageDir = std::filesystem::path(node.entry.packageDir);
        node.entry.sourceType = "workspace";
        node.entry.sourcePath = canonicalOrLexical(memberPath);
        if (!addAvailableNode(outNodes, node, outError)) {
            return false;
        }
        outWorkspacePackageIds.insert(node.entry.packageId);
    }

    return true;
}

const ProjectRegistryConfig* findRegistryConfig(
    const ProjectManifestData& manifest, std::string_view alias) {
    for (const auto& registry : manifest.registries) {
        if (registry.alias == alias) {
            return &registry;
        }
    }
    return nullptr;
}

bool packageIdsMatchDependencyPins(const std::vector<std::string>& manifestDeps,
                                   const std::vector<RegistryDependencyPin>& pins) {
    std::vector<std::string> pinnedIds;
    pinnedIds.reserve(pins.size());
    for (const auto& pin : pins) {
        pinnedIds.push_back(pin.packageId);
    }
    return sortedUniqueStrings(manifestDeps) == sortedUniqueStrings(std::move(pinnedIds));
}

bool findRegistryRecordForRequirement(const LoadedRegistryIndex& index,
                                      std::string_view packageId,
                                      const VersionRequirement& requirement,
                                      RegistryIndexRecord& outRecord,
                                      std::string& outError) {
    auto recordsIt = index.recordsByPackageId.find(std::string(packageId));
    if (recordsIt == index.recordsByPackageId.end()) {
        outError = "Registry does not contain '" + std::string(packageId) + "'.";
        return false;
    }

    for (const auto& record : recordsIt->second) {
        if (versionSatisfiesRequirement(record.parsedVersion, requirement)) {
            outRecord = record;
            return true;
        }
    }

    outError = "Registry does not contain a version of '" + std::string(packageId) +
               "' matching '" + requirement.raw + "'.";
    return false;
}

std::vector<RegistryIndexRecord> matchingRegistryRecords(
    const LoadedRegistryIndex& index, std::string_view packageId,
    const VersionRequirement& requirement) {
    std::vector<RegistryIndexRecord> matches;
    auto recordsIt = index.recordsByPackageId.find(std::string(packageId));
    if (recordsIt == index.recordsByPackageId.end()) {
        return matches;
    }

    for (const auto& record : recordsIt->second) {
        if (versionSatisfiesRequirement(record.parsedVersion, requirement)) {
            matches.push_back(record);
        }
    }
    return matches;
}

std::string availableNativeTargetsLabel(const RegistryIndexRecord& record) {
    std::vector<std::string> targets = record.nativeTargets;
    std::sort(targets.begin(), targets.end());
    targets.erase(std::unique(targets.begin(), targets.end()), targets.end());

    std::ostringstream out;
    for (size_t index = 0; index < targets.size(); ++index) {
        if (index != 0) {
            out << ", ";
        }
        out << targets[index];
    }
    return out.str();
}

bool selectRegistryArtifact(const RegistryIndexRecord& record,
                            std::string_view packageId,
                            const InstallOptions* options,
                            std::string& outArtifactPath,
                            std::string& outArtifactDigest,
                            std::string& outSelectedTarget,
                            std::string& outError) {
    outArtifactPath.clear();
    outArtifactDigest.clear();
    outSelectedTarget.clear();

    if (record.nativeTargets.empty()) {
        outArtifactPath = record.artifactPath;
        outArtifactDigest = record.artifactDigest;
        if (options != nullptr) {
            outSelectedTarget = effectiveInstallTarget(options);
        }
        return !outArtifactPath.empty() && !outArtifactDigest.empty();
    }

    const std::string desiredTarget = effectiveInstallTarget(options);
    auto targetIt = std::find(record.nativeTargets.begin(),
                              record.nativeTargets.end(), desiredTarget);
    if (targetIt != record.nativeTargets.end()) {
        const size_t index =
            static_cast<size_t>(std::distance(record.nativeTargets.begin(), targetIt));
        outArtifactPath = record.nativeArtifactPaths[index];
        outArtifactDigest = record.nativeArtifactDigests[index];
        outSelectedTarget = desiredTarget;
        return true;
    }

    if (options == nullptr && !record.nativeTargets.empty()) {
        outArtifactPath = record.nativeArtifactPaths.front();
        outArtifactDigest = record.nativeArtifactDigests.front();
        outSelectedTarget = record.nativeTargets.front();
        return true;
    }

    if (!record.artifactPath.empty() && !record.artifactDigest.empty()) {
        if (options != nullptr && options->noNativeBuild) {
            outError = "Registry package '" + std::string(packageId) +
                       "' does not publish a prebuilt native artifact for target '" +
                       desiredTarget + "'. Available targets: " +
                       availableNativeTargetsLabel(record) +
                       ". Source fallback is available in the registry entry, but --no-native-build forbids using it.";
            return false;
        }
        outArtifactPath = record.artifactPath;
        outArtifactDigest = record.artifactDigest;
        outSelectedTarget = desiredTarget;
        return true;
    }

    outError = "Registry package '" + std::string(packageId) +
               "' does not publish a native artifact for target '" +
               desiredTarget + "'. Available targets: " +
               availableNativeTargetsLabel(record) + ".";
    return false;
}

bool resolveRegistryPackageNode(
    const std::string& projectRoot, const ProjectManifestData& manifest,
    const std::string& registryAlias, const std::string& packageId,
    const std::string& requirementText,
    std::unordered_map<std::string, LoadedRegistryIndex>& loadedRegistryIndexes,
    std::unordered_map<std::string, PackageNode>& nodeCache,
    const InstallOptions* options, PackageNode& outNode, std::string& outError) {
    outNode = PackageNode{};

    VersionRequirement requirement;
    if (!parseVersionRequirement(requirementText, requirement, outError)) {
        outError = "Published dependency '" + packageId + "' has an invalid version requirement: " +
                   outError;
        return false;
    }

    const ProjectRegistryConfig* registry =
        findRegistryConfig(manifest, registryAlias);
    if (registry == nullptr) {
        outError = "Dependency '" + packageId + "' references unknown registry '" +
                   registryAlias + "'.";
        return false;
    }

    auto indexIt = loadedRegistryIndexes.find(registryAlias);
    if (indexIt == loadedRegistryIndexes.end()) {
        LoadedRegistryIndex loaded;
        if (!loadRegistryIndex(projectRoot, *registry, loaded, outError)) {
            return false;
        }
        indexIt = loadedRegistryIndexes.emplace(registryAlias, std::move(loaded)).first;
    }

    RegistryIndexRecord record;
    if (!findRegistryRecordForRequirement(indexIt->second, packageId, requirement,
                                          record, outError)) {
        outError = "Registry '" + registryAlias + "': " + outError;
        return false;
    }

    std::string rawArtifactPath;
    std::string artifactDigest;
    std::string selectedTarget;
    if (!selectRegistryArtifact(record, packageId, options, rawArtifactPath,
                                artifactDigest, selectedTarget, outError)) {
        return false;
    }

    const std::string cacheKey = registryAlias + "|" +
                                 registryRecordKey(packageId, record.version) +
                                 "|" + selectedTarget + "|" + rawArtifactPath +
                                 "|" + artifactDigest;
    auto nodeIt = nodeCache.find(cacheKey);
    if (nodeIt != nodeCache.end()) {
        outNode = nodeIt->second;
        return true;
    }
    std::filesystem::path artifactPath;
    if (!resolveManifestPath(indexIt->second.rootDir.string(), rawArtifactPath,
                             artifactPath, outError)) {
        return false;
    }
    artifactPath = canonicalOrLexical(artifactPath);
    if (!isDirectory(artifactPath)) {
        outError = "Registry artifact for '" + packageId + "@" + record.version +
                   "' is missing at '" + artifactPath.string() + "'.";
        return false;
    }

    const std::string actualDigest = digestDirectory(artifactPath);
    if (actualDigest.empty()) {
        outError = "Could not compute digest for registry artifact '" +
                   artifactPath.string() + "'.";
        return false;
    }
    if (actualDigest != artifactDigest) {
        outError = "Registry artifact digest mismatch for '" + packageId + "@" +
                   record.version + "'.";
        return false;
    }

    PackageNode node;
    if (!loadPackageEntryFromDir(artifactPath, node.entry, nullptr, outError)) {
        return false;
    }
    if (node.entry.packageId != packageId) {
        outError = "Registry artifact '" + artifactPath.string() +
                   "' declares package '" + node.entry.packageId +
                   "' but the index expects '" + packageId + "'.";
        return false;
    }
    if (node.entry.version != record.version) {
        outError = "Registry artifact '" + artifactPath.string() +
                   "' declares version '" + node.entry.version +
                   "' but the index expects '" + record.version + "'.";
        return false;
    }
    if (!packageIdsMatchDependencyPins(node.entry.dependencyIds, record.dependencies)) {
        outError = "Registry artifact '" + artifactPath.string() +
                   "' has dependency IDs that do not match the registry index for '" +
                   packageId + "@" + record.version + "'.";
        return false;
    }

    node.packageDir = artifactPath;
    node.registryDependencies = record.dependencies;
    node.entry.sourceType = "registry";
    node.entry.sourcePath = artifactPath.string();
    node.entry.registry = registryAlias;
    node.entry.artifactPath = artifactPath.string();
    node.entry.artifactDigest = artifactDigest;
    node.entry.selectedTarget.clear();
    node.entry.buildFromSource = false;
    if (node.entry.kind == "native") {
        const bool usesSourceArtifact =
            !record.artifactPath.empty() && rawArtifactPath == record.artifactPath;
        if (usesSourceArtifact) {
            const std::string desiredTarget =
                selectedTarget.empty()
                    ? (options != nullptr ? effectiveInstallTarget(options)
                                          : detectHostTarget())
                    : selectedTarget;
            if (options != nullptr && options->noNativeBuild) {
                outError = "Registry package '" + std::string(packageId) +
                           "' does not publish a prebuilt native artifact for target '" +
                           desiredTarget +
                           "'. Source fallback is available in the registry entry, but --no-native-build forbids using it.";
                return false;
            }
            if (desiredTarget != detectHostTarget()) {
                outError = "Registry package '" + std::string(packageId) +
                           "' does not publish a prebuilt native artifact for target '" +
                           desiredTarget +
                           "', and source-build fallback currently supports only the host target '" +
                           detectHostTarget() + "'.";
                return false;
            }
            node.entry.selectedTarget = desiredTarget;
            node.entry.buildFromSource = true;
            node.entry.libraryPath.clear();
        } else {
            node.entry.selectedTarget = selectedTarget;
        }
    }
    node.entry.dependencyIds.clear();
    for (const auto& pin : record.dependencies) {
        node.entry.dependencyIds.push_back(pin.packageId);
    }

    nodeCache.emplace(cacheKey, node);
    outNode = std::move(node);
    return true;
}

void ensureResolvedLibraryPath(PackageRegistryEntry& entry,
                               const std::filesystem::path& packageDir,
                               const std::string& projectRoot) {
    if (entry.kind != "native" || !entry.libraryPath.empty()) {
        return;
    }

    const std::string workspaceRoot =
        inferWorkspaceRoot(packageDir, entry, projectRoot);
    const std::filesystem::path candidate =
        std::filesystem::path(workspaceRoot) / "build" / "packages" /
        entry.packageNamespace / entry.packageName / kPackageLibraryFileName;
    if (fileExists(candidate)) {
        entry.libraryPath = canonicalOrLexical(candidate);
    }
}

void ensureResolvedLibraryPath(PackageNode& node, const std::string& projectRoot) {
    ensureResolvedLibraryPath(node.entry, node.packageDir, projectRoot);
}

bool stagePublishedNativeSourceArtifact(const std::filesystem::path& packagePath,
                                        std::filesystem::path& outStagingDir,
                                        std::string& outError) {
    outStagingDir.clear();

    if (!createTemporaryDirectory("mog-publish-native-source", outStagingDir,
                                  outError)) {
        return false;
    }

    if (!copyDirectoryRecursive(packagePath, outStagingDir, outError)) {
        return false;
    }

    std::ofstream apiHeader(outStagingDir / "NativePackageAPI.hpp");
    if (!apiHeader) {
        outError = "Could not write NativePackageAPI.hpp into '" +
                   outStagingDir.string() + "'.";
        return false;
    }
    apiHeader << kPublishedNativePackageApiHeader;

    return true;
}

bool stagePublishedNativePrebuiltArtifact(const std::filesystem::path& packagePath,
                                          const PackageManifest& manifest,
                                          const PackageRegistryEntry& packageEntry,
                                          std::filesystem::path& outStagingDir,
                                          std::string& outError) {
    outStagingDir.clear();

    if (packageEntry.libraryPath.empty() ||
        !fileExists(std::filesystem::path(packageEntry.libraryPath))) {
        outError = "Native package '" + packageEntry.packageId +
                   "' does not have a built library to publish.";
        return false;
    }

    if (!createTemporaryDirectory("mog-publish-native", outStagingDir, outError)) {
        return false;
    }

    const std::filesystem::path manifestPath =
        fileExists(packagePath / kProjectManifestFileName)
            ? packagePath / kProjectManifestFileName
            : packagePath / "package.toml";
    if (!fileExists(manifestPath)) {
        outError = "Could not find manifest for native package '" +
                   packageEntry.packageId + "'.";
        return false;
    }
    if (!copyFileReplacing(manifestPath, outStagingDir / manifestPath.filename(),
                           outError)) {
        return false;
    }

    if (!packageEntry.apiPath.empty() &&
        !copyFileReplacing(packageEntry.apiPath, outStagingDir / kPackageApiFileName,
                           outError)) {
        return false;
    }

    const std::filesystem::path libraryDestination =
        manifest.library.empty() ? outStagingDir / kPackageLibraryFileName
                                 : outStagingDir / manifest.library;
    if (!copyFileReplacing(packageEntry.libraryPath, libraryDestination, outError)) {
        return false;
    }

    return true;
}

bool writeGeneratedNativeBuildCMakeLists(const std::filesystem::path& outputPath,
                                         const std::filesystem::path& packageDir,
                                         std::string& outError) {
    std::ofstream out(outputPath);
    if (!out) {
        outError = "Could not open '" + outputPath.string() + "' for writing.";
        return false;
    }

    out << "cmake_minimum_required(VERSION 3.10)\n"
        << "project(mog_native_package LANGUAGES CXX)\n"
        << "set(CMAKE_CXX_STANDARD 17)\n"
        << "set(CMAKE_CXX_STANDARD_REQUIRED ON)\n"
        << "add_library(mog_native_package MODULE "
        << quoteTomlString((packageDir / "package.cpp").string()) << ")\n"
        << "target_include_directories(mog_native_package PRIVATE "
        << quoteTomlString(packageDir.string()) << ")\n"
        << "set_target_properties(mog_native_package PROPERTIES PREFIX \"\" OUTPUT_NAME \"package\")\n";
    return true;
}

bool locateBuiltNativeLibrary(const std::filesystem::path& buildDir,
                              std::filesystem::path& outLibraryPath,
                              std::string& outError) {
    outLibraryPath.clear();

    std::vector<std::filesystem::path> matches;
    std::error_code ec;
    for (std::filesystem::recursive_directory_iterator it(buildDir, ec), end;
         !ec && it != end; it.increment(ec)) {
        if (!it->is_regular_file(ec) || ec) {
            continue;
        }
        if (it->path().filename() == kPackageLibraryFileName) {
            matches.push_back(it->path());
        }
    }
    if (ec) {
        outError = "Could not scan build output directory '" + buildDir.string() +
                   "' for native libraries.";
        return false;
    }
    if (matches.empty()) {
        outError = "Native source build completed but did not produce '" +
                   std::string(kPackageLibraryFileName) + "'.";
        return false;
    }

    std::sort(matches.begin(), matches.end());
    outLibraryPath = matches.front();
    return true;
}

bool runLoggedSystemCommand(const std::string& command,
                            const std::filesystem::path& logPath) {
    const std::string fullCommand =
        command + " >> " + shellQuote(logPath.string()) + " 2>&1";
    return std::system(fullCommand.c_str()) == 0;
}

bool buildNativePackageFromSource(const PackageRegistryEntry& sourceEntry,
                                  const std::filesystem::path& cacheDir,
                                  std::string& outError) {
    const std::filesystem::path packageCpp = cacheDir / "package.cpp";
    const std::filesystem::path packageCmake = cacheDir / "CMakeLists.txt";
    PackageManifest manifest;
    std::string manifestError;
    const bool hasManifest =
        loadPackageManifest(cacheDir.string(), manifest, manifestError);
    const std::string sourceBuildPrefix =
        buildNativeSourceFallbackPrefix(sourceEntry);
    const std::string systemDependencyText =
        hasManifest ? formatSystemDependencies(manifest.systemDependencies) : "";

    if (!fileExists(packageCpp) && !fileExists(packageCmake)) {
        outError = "Native package '" + sourceEntry.packageId +
                   "' does not include package.cpp or CMakeLists.txt for source-build fallback.";
        return false;
    }

    const std::filesystem::path buildLogPath = cacheDir / ".build.log";
    std::error_code clearLogError;
    std::filesystem::remove(buildLogPath, clearLogError);
    if (!runLoggedSystemCommand("cmake --version", buildLogPath)) {
        outError = sourceBuildPrefix +
                   " requires CMake on PATH." + systemDependencyText;
        return false;
    }

    std::filesystem::path buildDir;
    if (!createTemporaryDirectory("mog-native-build", buildDir, outError)) {
        return false;
    }
    ScopedPathCleanup buildCleanup{buildDir};

    std::filesystem::path sourceDir = cacheDir;
    std::filesystem::path generatedSourceDir;
    ScopedPathCleanup generatedSourceCleanup;
    if (!fileExists(packageCmake)) {
        if (!createTemporaryDirectory("mog-native-build-src", generatedSourceDir,
                                      outError)) {
            return false;
        }
        generatedSourceCleanup.path = generatedSourceDir;
        if (!writeGeneratedNativeBuildCMakeLists(generatedSourceDir / "CMakeLists.txt",
                                                 cacheDir, outError)) {
            return false;
        }
        sourceDir = generatedSourceDir;
    }

    const std::string configureCommand =
        "cmake -S " + shellQuote(sourceDir.string()) + " -B " +
        shellQuote(buildDir.string()) + " -DCMAKE_BUILD_TYPE=Release";
    if (!runLoggedSystemCommand(configureCommand, buildLogPath)) {
        const std::string buildLog = readFileText(buildLogPath);
        const SystemDependencySpec* missingDependency =
            hasManifest ? detectMissingSystemDependency(manifest.systemDependencies,
                                                       buildLog)
                        : nullptr;
        if (missingDependency != nullptr) {
            outError = sourceBuildPrefix + " could not find required system dependency '" +
                       missingDependency->name + "'." + systemDependencyText +
                       " See '" + buildLogPath.string() + "'.";
        } else {
            outError = sourceBuildPrefix + " configure step failed." +
                       systemDependencyText + " See '" +
                       buildLogPath.string() + "'.";
        }
        return false;
    }

    const std::string buildCommand =
        "cmake --build " + shellQuote(buildDir.string()) + " --config Release";
    if (!runLoggedSystemCommand(buildCommand, buildLogPath)) {
        const std::string buildLog = readFileText(buildLogPath);
        const SystemDependencySpec* missingDependency =
            hasManifest ? detectMissingSystemDependency(manifest.systemDependencies,
                                                       buildLog)
                        : nullptr;
        if (missingDependency != nullptr) {
            outError = sourceBuildPrefix + " could not find required system dependency '" +
                       missingDependency->name + "'." + systemDependencyText +
                       " See '" + buildLogPath.string() + "'.";
        } else {
            outError = sourceBuildPrefix + " failed." + systemDependencyText +
                       " See '" + buildLogPath.string() + "'.";
        }
        return false;
    }

    std::filesystem::path builtLibrary;
    if (!locateBuiltNativeLibrary(buildDir, builtLibrary, outError)) {
        const std::string buildLog = readFileText(buildLogPath);
        const SystemDependencySpec* missingDependency =
            hasManifest ? detectMissingSystemDependency(manifest.systemDependencies,
                                                       buildLog)
                        : nullptr;
        if (missingDependency != nullptr) {
            outError = sourceBuildPrefix + " could not find required system dependency '" +
                       missingDependency->name + "'." + systemDependencyText +
                       " See '" + buildLogPath.string() + "'.";
        } else {
            outError = sourceBuildPrefix + " did not produce '" +
                       std::string(kPackageLibraryFileName) + "'." +
                       systemDependencyText + " See '" +
                       buildLogPath.string() + "'.";
        }
        return false;
    }

    if (!copyFileReplacing(builtLibrary, cacheDir / kPackageLibraryFileName,
                           outError)) {
        return false;
    }

    return true;
}

bool resolveDependencyNode(
    const std::string& projectRoot, const DependencySpec& dependency,
    const std::unordered_map<std::string, PackageNode>& available,
    const std::unordered_set<std::string>& workspacePackageIds, PackageNode& outNode,
    std::string& outError) {
    outNode = PackageNode{};

    if (dependency.workspace) {
        if (dependency.packageId.empty()) {
            outError = "Workspace dependency '" + dependency.alias +
                       "' must declare package = \"namespace:name\".";
            return false;
        }

        auto it = available.find(dependency.packageId);
        if (it == available.end() ||
            workspacePackageIds.find(dependency.packageId) ==
                workspacePackageIds.end()) {
            outError = "Workspace dependency '" + dependency.alias +
                       "' could not find workspace member '" +
                       dependency.packageId + "'.";
            return false;
        }
        outNode = it->second;
        outNode.entry.sourceType = "workspace";
        outNode.entry.sourcePath = outNode.packageDir.string();
    } else if (!dependency.path.empty()) {
        const std::filesystem::path dependencyPath =
            std::filesystem::path(projectRoot) / dependency.path;
        if (!loadPackageEntryFromDir(dependencyPath, outNode.entry, nullptr,
                                     outError)) {
            outError = "Could not load dependency '" + dependency.alias + "': " +
                       outError;
            return false;
        }

        outNode.packageDir = std::filesystem::path(outNode.entry.packageDir);
        outNode.entry.sourceType = "path";
        outNode.entry.sourcePath = canonicalOrLexical(dependencyPath);
    } else if (!dependency.git.empty()) {
        outError = "Dependency '" + dependency.alias +
                   "' uses git sources, which are recognized but not implemented until Phase 2.";
        return false;
    } else if (!dependency.registry.empty()) {
        outError = "Dependency '" + dependency.alias +
                   "' uses named registries, which are recognized but not implemented until Phase 2.";
        return false;
    } else if (!dependency.packageId.empty()) {
        outError = "Dependency '" + dependency.alias +
                   "' requires published package resolution, which is not implemented until Phase 2.";
        return false;
    } else {
        outError = "Dependency '" + dependency.alias + "' does not declare a usable source.";
        return false;
    }

    ensureResolvedLibraryPath(outNode, projectRoot);

    if (!dependency.packageId.empty() &&
        dependency.packageId != outNode.entry.packageId) {
        outError = "Dependency '" + dependency.alias + "' declares package '" +
                   dependency.packageId + "' but resolves to '" +
                   outNode.entry.packageId + "'.";
        return false;
    }

    if (!dependency.version.empty()) {
        VersionRequirement requirement;
        if (!parseVersionRequirement(dependency.version, requirement, outError)) {
            outError = "Dependency '" + dependency.alias + "' has an invalid version requirement: " +
                       outError;
            return false;
        }

        SemanticVersion localVersion;
        if (!parseSemanticVersion(outNode.entry.version, localVersion) ||
            !versionSatisfiesRequirement(localVersion, requirement)) {
            outError = "Dependency '" + dependency.alias + "' requires version '" +
                       dependency.version + "' but local package '" +
                       outNode.entry.packageId + "' reports '" +
                       outNode.entry.version + "'.";
            return false;
        }
    }

    return true;
}

bool validatePackageEntry(const PackageRegistryEntry& entry,
                          const std::string& projectRoot,
                          std::string& outError) {
    const std::filesystem::path packageDir(entry.packageDir);
    if (entry.kind == "native") {
        if (entry.sourceType == "registry") {
            PackageManifest manifest;
            if (!loadPackageManifest(packageDir.string(), manifest, outError)) {
                return false;
            }
            if (makePackageId(manifest.packageNamespace, manifest.packageName) !=
                entry.packageId) {
                outError = "Registry native package '" + entry.packageId +
                           "' is backed by manifest '" +
                           makePackageId(manifest.packageNamespace,
                                         manifest.packageName) +
                           "'.";
                return false;
            }

            if (entry.apiPath.empty() ||
                !fileExists(std::filesystem::path(entry.apiPath))) {
                outError = "Native package '" + entry.packageId +
                           "' is missing package.api.mog.";
                return false;
            }

            PackageApiMetadata apiMetadata;
            if (!loadPackageApiMetadata(entry.apiPath, entry.packageId,
                                        entry.importName, apiMetadata, outError)) {
                return false;
            }
            if (entry.buildFromSource) {
                if (!fileExists(packageDir / "package.cpp") &&
                    !fileExists(packageDir / "CMakeLists.txt")) {
                    outError = "Native package '" + entry.packageId +
                               "' does not include package.cpp or CMakeLists.txt for source-build fallback.";
                    return false;
                }
                return true;
            }

            if (entry.libraryPath.empty() ||
                !fileExists(std::filesystem::path(entry.libraryPath))) {
                outError = "Native package '" + entry.packageId +
                           "' does not have a built library to install.";
                return false;
            }

            NativePackageDescriptor descriptor;
            if (!loadNativePackageDescriptor(entry.libraryPath, descriptor, outError,
                                             false, nullptr)) {
                return false;
            }
            if (descriptor.packageId != entry.packageId) {
                outError = "Registry native package '" + entry.packageId +
                           "' is backed by library '" + descriptor.packageId +
                           "'.";
                return false;
            }

            return validateNativePackageApi(apiMetadata, descriptor, outError);
        }

        const std::string validationRoot =
            inferWorkspaceRoot(packageDir, entry, projectRoot);
        return validatePackageDirectory(packageDir.string(), validationRoot,
                                        outError);
    }

    if (entry.entryPath.empty() || !fileExists(entry.entryPath)) {
        outError = "Source package '" + entry.importName +
                   "' is missing entry module '" + entry.entryPath + "'.";
        return false;
    }

    if (!entry.apiPath.empty()) {
        PackageApiMetadata apiMetadata;
        if (!loadPackageApiMetadata(entry.apiPath, entry.packageId, entry.importName,
                                    apiMetadata, outError)) {
            return false;
        }
    }

    return true;
}

bool materializeCacheEntry(const PackageRegistryEntry& sourceEntry,
                           const InstallOptions& options,
                           const std::filesystem::path& cacheDir,
                           std::string& outError) {
    if (!removePathIfExists(cacheDir, outError)) {
        return false;
    }

    std::error_code ec;
    std::filesystem::create_directories(cacheDir, ec);
    if (ec) {
        outError = "Could not create cache directory '" + cacheDir.string() + "'.";
        return false;
    }

    const std::filesystem::path sourceDir(sourceEntry.sourcePath);
    if (sourceEntry.kind == "source") {
        if (!copyDirectoryRecursive(sourceDir, cacheDir, outError)) {
            return false;
        }
        return writeCacheMetadataFile(cacheMetadataPath(cacheDir), sourceEntry,
                                      outError);
    }

    if (sourceEntry.buildFromSource) {
        if (!copyDirectoryRecursive(sourceDir, cacheDir, outError)) {
            return false;
        }
        if (!buildNativePackageFromSource(sourceEntry, cacheDir, outError)) {
            return false;
        }
        return writeCacheMetadataFile(cacheMetadataPath(cacheDir), sourceEntry,
                                      outError);
    }

    const std::filesystem::path sourceManifest = fileExists(sourceDir / kProjectManifestFileName)
                                                     ? sourceDir / kProjectManifestFileName
                                                     : sourceDir / "package.toml";
    if (!copyFileReplacing(sourceManifest, cacheDir / kProjectManifestFileName,
                           outError)) {
        return false;
    }

    if (!sourceEntry.apiPath.empty() &&
        !copyFileReplacing(sourceEntry.apiPath, cacheDir / kPackageApiFileName,
                           outError)) {
        return false;
    }

    if (sourceEntry.libraryPath.empty()) {
        if (!sourceEntry.selectedTarget.empty()) {
            if (options.noNativeBuild) {
                outError = "Native package '" + sourceEntry.importName +
                           "' does not publish a prebuilt library for target '" +
                           sourceEntry.selectedTarget +
                           "', and --no-native-build forbids source fallback.";
            } else {
                outError = "Native package '" + sourceEntry.importName +
                           "' does not publish a prebuilt library for target '" +
                           sourceEntry.selectedTarget +
                           "'. Source-build fallback is not implemented yet for this runtime installation.";
            }
            return false;
        }
        outError = "Native package '" + sourceEntry.importName +
                   "' does not have a built library to install.";
        return false;
    }

    if (!copyFileReplacing(sourceEntry.libraryPath,
                           cacheDir / kPackageLibraryFileName, outError)) {
        return false;
    }

    return writeCacheMetadataFile(cacheMetadataPath(cacheDir), sourceEntry, outError);
}

bool materializeProjectInstall(const PackageRegistryEntry& sourceEntry,
                               const std::filesystem::path& projectRoot,
                               const InstallOptions& options,
                               PackageRegistryEntry& outInstalled,
                               std::string& outError) {
    outInstalled = sourceEntry;

    const std::string digestSeed = sourceEntry.version + "|" +
                                   sourceEntry.registry + "|" +
                                   sourceEntry.artifactPath + "|" +
                                   sourceEntry.artifactDigest + "|" +
                                   sourceEntry.selectedTarget + "|" +
                                   (sourceEntry.buildFromSource ? "source-build|" : "prebuilt|") +
                                   sourceEntry.manifestDigest + "|" +
                                   sourceEntry.apiDigest + "|" +
                                   sourceEntry.sourcePath;
    const std::string installKey = hashString(digestSeed);
    const std::filesystem::path cacheRoot =
        preferredLocalCacheRoot(projectRoot);
    const std::filesystem::path cacheDir =
        cacheRoot / sourceEntry.packageNamespace / sourceEntry.packageName /
        installKey;
    const std::filesystem::path installDir =
        installPackagesRoot(projectRoot) / sourceEntry.packageNamespace /
        sourceEntry.packageName;

    CacheEntryMetadata metadata;
    std::string metadataError;
    const bool hasReusableCache =
        isDirectory(cacheDir) && fileExists(cacheMetadataPath(cacheDir)) &&
        loadCacheMetadataFile(cacheMetadataPath(cacheDir), metadata, metadataError) &&
        cacheMetadataMatchesEntry(metadata, sourceEntry);

    if (!hasReusableCache) {
        if (options.offline && sourceEntry.sourceType == "registry") {
            outError = "Dependency '" + sourceEntry.packageId +
                       "' cannot be installed with --offline because its registry artifact is not cached locally.";
            return false;
        }
        if (!materializeCacheEntry(sourceEntry, options, cacheDir, outError)) {
            return false;
        }
    }

    if (!removePathIfExists(installDir, outError)) {
        return false;
    }
    if (!ensureParentDirectory(installDir, outError)) {
        return false;
    }

    if (!copyDirectoryRecursive(cacheDir, installDir, outError)) {
        return false;
    }

    outInstalled.packageDir = installDir.lexically_normal().string();
    outInstalled.sourcePath = canonicalOrLexical(sourceEntry.sourcePath);
    outInstalled.artifactPath = canonicalOrLexical(sourceEntry.artifactPath);
    outInstalled.selectedTarget = sourceEntry.selectedTarget;

    const std::filesystem::path installedApi = installDir / kPackageApiFileName;
    if (fileExists(installedApi)) {
        outInstalled.apiPath = installedApi.lexically_normal().string();
    } else {
        outInstalled.apiPath.clear();
    }

    if (sourceEntry.kind == "source") {
        std::filesystem::path relativeEntry =
            std::filesystem::relative(std::filesystem::path(sourceEntry.entryPath),
                                      std::filesystem::path(sourceEntry.sourcePath));
        outInstalled.entryPath = (installDir / relativeEntry).lexically_normal().string();
        outInstalled.libraryPath.clear();
    } else {
        outInstalled.libraryPath =
            (installDir / kPackageLibraryFileName).lexically_normal().string();
        outInstalled.entryPath.clear();
    }

    return validatePackageEntry(outInstalled, projectRoot.string(), outError);
}

