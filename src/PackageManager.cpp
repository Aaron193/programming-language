bool createTemporaryDirectory(std::string_view prefix,
                              std::filesystem::path& outPath,
                              std::string& outError) {
    std::error_code ec;
    const std::filesystem::path tempRoot =
        std::filesystem::temp_directory_path(ec);
    if (ec) {
        outError = "Could not resolve the system temporary directory.";
        return false;
    }

    for (int attempt = 0; attempt < 1024; ++attempt) {
        const std::string suffix =
            hashString(std::string(prefix) + "|" + std::to_string(std::rand()) +
                       "|" + std::to_string(attempt));
        const std::filesystem::path candidate =
            tempRoot / (std::string(prefix) + "-" + suffix);
        std::filesystem::create_directories(candidate, ec);
        if (!ec) {
            outPath = candidate;
            return true;
        }
        if (ec != std::errc::file_exists) {
            outError = "Could not create temporary directory '" +
                       candidate.string() + "'.";
            return false;
        }
        ec.clear();
    }

    outError = "Could not allocate a unique temporary directory.";
    return false;
}

std::string inferWorkspaceRoot(const std::filesystem::path& packageDir,
                               const PackageRegistryEntry& entry,
                               const std::string& fallbackProjectRoot) {
    std::filesystem::path current = packageDir;
    while (!current.empty()) {
        const std::filesystem::path candidateLibrary =
            current / "build" / "packages" / entry.packageNamespace /
            entry.packageName / kPackageLibraryFileName;
        if (fileExists(candidateLibrary)) {
            return current.string();
        }
        if (current == current.root_path()) {
            break;
        }
        current = current.parent_path();
    }

    return fallbackProjectRoot;
}

bool loadPackageEntryFromDir(const std::filesystem::path& packageDir,
                             PackageRegistryEntry& outEntry,
                             std::vector<std::string>* outDependencies,
                             std::string& outError) {
    PackageManifest manifest;
    if (!loadPackageManifest(packageDir.string(), manifest, outError)) {
        return false;
    }

    outEntry = PackageRegistryEntry{};
    outEntry.importName = manifest.importName.empty() ? manifest.packageName
                                                      : manifest.importName;
    outEntry.packageNamespace = manifest.packageNamespace;
    outEntry.packageName = manifest.packageName;
    outEntry.packageId = makePackageId(manifest.packageNamespace, manifest.packageName);
    outEntry.version = manifest.version;
    outEntry.packageDir = canonicalOrLexical(packageDir);
    outEntry.kind = manifest.kind.empty() ? "native" : manifest.kind;
    outEntry.description = manifest.description;
    outEntry.dependencyIds.clear();
    for (const auto& dependency : manifest.dependencies) {
        outEntry.dependencyIds.push_back(dependency.packageId);
    }
    outEntry.sourceType = "path";
    outEntry.sourcePath = canonicalOrLexical(packageDir);
    outEntry.manifestDigest = manifestDigestForDir(packageDir);

    if (outEntry.kind == "source") {
        std::filesystem::path entryPath = manifest.entry.empty()
                                              ? packageDir / "src" / "main.mog"
                                              : packageDir / manifest.entry;
        outEntry.entryPath = canonicalOrLexical(entryPath);
    } else if (!manifest.library.empty()) {
        const std::filesystem::path configuredLibrary = packageDir / manifest.library;
        if (fileExists(configuredLibrary)) {
            outEntry.libraryPath = canonicalOrLexical(configuredLibrary);
        }
    } else {
        const std::filesystem::path directLibrary =
            packageDir / kPackageLibraryFileName;
        if (fileExists(directLibrary)) {
            outEntry.libraryPath = canonicalOrLexical(directLibrary);
        }
    }

    const std::filesystem::path apiPath = packageDir / kPackageApiFileName;
    if (fileExists(apiPath)) {
        outEntry.apiPath = canonicalOrLexical(apiPath);
        outEntry.apiDigest = digestFile(apiPath);
    }

    if (outDependencies != nullptr) {
        *outDependencies = outEntry.dependencyIds;
    }

    return true;
}

std::string normalizePackageIdLikeSpecifier(const std::string& rawSpecifier) {
    if (rawSpecifier.find(':') != std::string::npos) {
        return rawSpecifier;
    }

    const size_t slash = rawSpecifier.find('/');
    if (slash == std::string::npos) {
        return "";
    }

    return rawSpecifier.substr(0, slash) + ":" + rawSpecifier.substr(slash + 1);
}

bool parseDependencySpecifier(const std::string& rawSpecifier,
                              std::string& outPackageId,
                              std::string& outVersionRequirement) {
    outPackageId.clear();
    outVersionRequirement.clear();

    const size_t at = rawSpecifier.rfind('@');
    std::string packageText = rawSpecifier;
    if (at != std::string::npos && at > 0) {
        packageText = rawSpecifier.substr(0, at);
        outVersionRequirement = rawSpecifier.substr(at + 1);
    }

    outPackageId = normalizePackageIdLikeSpecifier(packageText);
    return !outPackageId.empty();
}

std::string defaultPublishedRegistryAlias(const ProjectManifestData& manifest) {
    if (findRegistryConfig(manifest, "default") != nullptr) {
        return "default";
    }
    if (manifest.registries.size() == 1) {
        return manifest.registries.front().alias;
    }
    return "";
}

bool relativePathString(const std::filesystem::path& from,
                        const std::filesystem::path& to,
                        std::string& outPath) {
    std::error_code ec;
    std::filesystem::path relative = std::filesystem::relative(to, from, ec);
    if (ec) {
        outPath = normalizeRelativePath(to.lexically_normal().string());
        return false;
    }
    outPath = normalizeRelativePath(relative.lexically_normal().string());
    return true;
}

